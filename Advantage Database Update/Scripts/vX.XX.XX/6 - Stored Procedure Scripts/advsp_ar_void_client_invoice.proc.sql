						GL_ACCT_CITY, MARKET_CODE, MEDIA_MONTH, MEDIA_YEAR, [START_DATE], END_DATE, -EMP_TIME_AMT, -INC_ONLY_AMT, -COMMISSION_AMT, -REBATE_AMT, -COST_AMT, -NET_CHARGE_AMT, -NON_RESALE_AMT, -DISCOUNT_AMT, -ADDITIONAL_AMT,
