CREATE PROCEDURE [dbo].[advsp_media_manager_delete_selections]
	@user_id varchar(100)
AS

	UPDATE dbo.INTERNET_HEADER SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)
	
	UPDATE dbo.MAGAZINE_HEADER SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)

	UPDATE dbo.NEWSPAPER_HEADER SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)

	UPDATE dbo.OUTDOOR_HEADER SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)

	UPDATE dbo.RADIO_HDR SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)

	UPDATE dbo.TV_HDR SET LOCKED_BY = NULL
	WHERE UPPER(LOCKED_BY) = UPPER(@user_id)
	
	UPDATE dbo.PURCHASE_ORDER SET LOCK_USER = NULL
	WHERE UPPER(LOCK_USER) = UPPER(@user_id)

GO
