IF NOT EXISTS(SELECT AGY_SETTINGS_CODE FROM AGY_SETTINGS_LOOKUP WHERE AGY_SETTINGS_CODE = 'INVPRT_FILE_NAME' AND LOOKUP_VALUE = 4) BEGIN

	INSERT INTO
		dbo.AGY_SETTINGS_LOOKUP(AGY_SETTINGS_CODE, LOOKUP_DISPLAY, LOOKUP_VALUE)
	VALUES
		('INVPRT_FILE_NAME', 'Invoice Number, Sequence, CDP Codes, Campaign Code', 4)

END ELSE BEGIN

	UPDATE
		dbo.AGY_SETTINGS_LOOKUP
    SET
        LOOKUP_DISPLAY = 'Invoice Number, Sequence, CDP Codes, Campaign Code'
    WHERE
        AGY_SETTINGS_CODE = 'INVPRT_FILE_NAME' AND
        LOOKUP_VALUE = 4

END
