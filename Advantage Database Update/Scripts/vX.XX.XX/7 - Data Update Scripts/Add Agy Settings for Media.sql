IF EXISTS(SELECT APP_DESC FROM dbo.AGY_SETTINGS_APP WHERE AGY_SETTINGS_APP = 11) BEGIN

	DELETE FROM 
		[dbo].[AGY_SETTINGS_APP] 
	WHERE 
		AGY_SETTINGS_APP = 11

END
GO

IF EXISTS(SELECT TAB_DESC FROM dbo.AGY_SETTINGS_TAB WHERE AGY_SETTINGS_APP = 11 AND AGY_SETTINGS_TAB = 1) BEGIN

	DELETE FROM 
		[dbo].[AGY_SETTINGS_TAB]
	WHERE 
		AGY_SETTINGS_APP = 11 
		AND AGY_SETTINGS_TAB = 1

END
GO

IF NOT EXISTS(SELECT AGY_SETTINGS_CODE FROM dbo.AGY_SETTINGS WHERE AGY_SETTINGS_CODE = 'MEDIA_AUTO_BUYER') BEGIN

