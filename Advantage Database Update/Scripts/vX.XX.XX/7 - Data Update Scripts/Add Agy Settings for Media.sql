	VALUES 
		('MEDIA_AUTO_BUYER', 'Automatically place the buyer on a media plan or broadcast worksheet', 0, 0, 0, 1, NULL, NULL, NULL, 16)

END ELSE BEGIN

	UPDATE
		dbo.AGY_SETTINGS
	SET 
		AGY_SETTINGS_DESC = 'Automatically place the buyer on a media plan or broadcast worksheet', 
		AGY_SETTINGS_DEF = 0, 
		AGY_SETTINGS_MIN = 0, 
		AGY_SETTINGS_MAX = 1, 
		AGY_SETTINGS_APP = NULL, 
		AGY_SETTINGS_TAB = NULL,  
