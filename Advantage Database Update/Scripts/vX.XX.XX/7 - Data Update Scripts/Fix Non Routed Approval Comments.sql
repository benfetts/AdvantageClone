UPDATE ALERT_COMMENT SET PROOFING_STATUS_ID = D.PROOFING_STATUS_ID
FROM
	ALERT A
	INNER JOIN ALERT_RCPT_DISMISSED D ON A.ALERT_ID = D.ALERT_ID
	INNER JOIN ALERT_COMMENT AC ON A.ALERT_ID = AC.ALERT_ID
WHERE
	A.ALERT_CAT_ID = 79
	AND A.ALRT_NOTIFY_HDR_ID IS NULL 
	AND A.ALERT_STATE_ID IS NULL
	AND D.PROOFING_STATUS_ID IS NOT NULL
	AND AC.PROOFING_STATUS_ID IS NULL
	AND AC.DOCUMENT_ID IS NOT NULL
	AND (AC.COMMENT LIKE 'APPROVED |%' OR AC.COMMENT LIKE 'REJECTED |%' OR AC.COMMENT LIKE 'DEFERRED |%')
;	