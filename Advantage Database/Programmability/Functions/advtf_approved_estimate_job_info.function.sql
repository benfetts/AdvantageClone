CREATE FUNCTION [dbo].[advtf_approved_estimate_job_info](
@JOB_NUMBER INT,
@JOB_COMPONENT_NBR SMALLINT,
@FNC_CODE VARCHAR(6),
@EMP_CODE VARCHAR(6),
@EMP_ONLY BIT
)
RETURNS 
@RETURN_TABLE TABLE (
	JOB_NUMBER INT,
	JOB_COMPONENT_NBR SMALLINT,
	ESTIMATE_NUMBER INT,
	EST_LOG_DESC  VARCHAR(60),
	EST_COMPONENT_NBR SMALLINT,
	EST_COMP_DESC VARCHAR(60),
	EST_QUOTE_NBR SMALLINT,
	EST_REV_NBR SMALLINT,
	FNC_CODE VARCHAR(6),
	EST_REV_SUP_BY_CODE VARCHAR(6),
	SUM_EST_REV_QUANTITY DECIMAL(32,2),
	SUM_EST_AMOUNT DECIMAL(32,2),
	EXT_CONTINGENCY DECIMAL (14,2)
)
AS
BEGIN

	DECLARE
		@HAS_APPROVED_ESTIMATE BIT,
		@HAS_APPROVED_ESTIMATE_INTERNAL BIT

	SET @HAS_APPROVED_ESTIMATE = 0
	SET @HAS_APPROVED_ESTIMATE_INTERNAL = 0

    -- SEE IF THERE IS A CLIENT APPROVAL
	IF EXISTS (SELECT JOB_NUMBER FROM ESTIMATE_APPROVAL WITH(NOLOCK) WHERE JOB_NUMBER =  @JOB_NUMBER AND JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR)
	BEGIN
		SET @HAS_APPROVED_ESTIMATE = 1;
