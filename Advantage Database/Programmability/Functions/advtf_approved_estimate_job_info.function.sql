				ESTIMATE_REV_DET.ESTIMATE_NUMBER = ESTIMATE_APPROVAL.ESTIMATE_NUMBER INNER JOIN
				ESTIMATE_REV ON ESTIMATE_REV_DET.ESTIMATE_NUMBER = ESTIMATE_REV.ESTIMATE_NUMBER AND 
