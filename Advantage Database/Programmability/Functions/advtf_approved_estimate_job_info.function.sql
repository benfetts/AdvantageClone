		IF EXISTS (SELECT JOB_NUMBER FROM ESTIMATE_INT_APPR WITH(NOLOCK) WHERE JOB_NUMBER =  @JOB_NUMBER AND JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR)
		BEGIN
