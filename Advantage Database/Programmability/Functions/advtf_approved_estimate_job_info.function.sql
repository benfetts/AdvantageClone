				(ESTIMATE_REV_DET.EST_FNC_TYPE = 'E') 
				AND ESTIMATE_APPROVAL.JOB_NUMBER = @JOB_NUMBER 
				AND ESTIMATE_APPROVAL.JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR 
				AND	ESTIMATE_REV_DET.FNC_CODE = @FNC_CODE
			GROUP BY 
				ESTIMATE_REV_DET.EST_REV_EXT_AMT, ESTIMATE_REV_DET.EXT_MARKUP_AMT, ESTIMATE_APPROVAL.JOB_NUMBER, 
				ESTIMATE_APPROVAL.JOB_COMPONENT_NBR, ESTIMATE_LOG.ESTIMATE_NUMBER, ESTIMATE_LOG.EST_LOG_DESC, 
				ESTIMATE_COMPONENT.EST_COMPONENT_NBR, ESTIMATE_COMPONENT.EST_COMP_DESC, ESTIMATE_QUOTE.EST_QUOTE_NBR, 
				ESTIMATE_REV.EST_REV_NBR, ESTIMATE_REV_DET.FNC_CODE, ESTIMATE_REV_DET.EST_REV_SUP_BY_CDE, ESTIMATE_REV_DET.EXT_CONTINGENCY;
		END

