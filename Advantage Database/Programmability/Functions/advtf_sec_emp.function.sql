--DROP FUNCTION [dbo].[advtf_sec_emp]
CREATE FUNCTION [dbo].[advtf_sec_emp] (@USER_CODE VARCHAR(100))
RETURNS @EMP_CODE TABLE (EMP_CODE VARCHAR(6))
AS
BEGIN

	INSERT INTO @EMP_CODE
	SELECT EMP_CODE FROM SEC_EMP WHERE UPPER(SEC_EMP.USER_ID) = UPPER(@USER_CODE)
	UNION
	SELECT EMP_CODE FROM SEC_USER WHERE UPPER(SEC_USER.USER_CODE) = UPPER(@USER_CODE);

	RETURN;

END;