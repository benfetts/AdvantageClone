CREATE PROCEDURE advsp_ar_invoice_has_closed_job @AR_INV_NBR int

AS

IF EXISTS(SELECT A.JOB_NUMBER 
			FROM dbo.ARINV_JOB A
				INNER JOIN dbo.JOB_COMPONENT J ON A.JOB_NUMBER = J.JOB_NUMBER AND A.JOB_COMPONENT_NBR = J.JOB_COMPONENT_NBR 
			WHERE
					A.AR_INV_NBR = @AR_INV_NBR 
			AND		J.JOB_PROCESS_CONTRL IN (2, 6, 12)
			)
	SELECT 1
ELSE
	SELECT 0

GO