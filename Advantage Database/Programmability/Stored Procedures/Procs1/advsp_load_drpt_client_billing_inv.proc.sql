	DECLARE @ROW_ID_TYPE AS INT,
			@CURR_AP_ID_TYPE AS INTEGER,
	        @CURR_FEE DECIMAL(18,6),
			@CURR_COST DECIMAL(18,6),
			@CURR_AP_CHK_AMT_TYPE DECIMAL(18,6),
			@TYPE AS VARCHAR(50),
			@Client varchar(6),
			@ClientDesc varchar(40),
			@Division varchar(6),
			@DivisionDesc varchar(40),
			@Product varchar(6),
			@ProductDesc varchar(40),
			@PP varchar(6), @CURR_AP_CHK_AMT_UNCLEARED_TYPE DECIMAL(18,6)

	DECLARE MY_ROWS_TYPE                         CURSOR  
        FOR
	        SELECT ID,AP_ID, [ClientCode],[ClientDescription],[DivisionCode],[DivisionDescription],[ProductCode],[ProductDescription],[PostingPeriod], [Type], FeeAmount, CostAmount
	        FROM   #CLIENT_BILLING_TYPE
			WHERE AP_ID <> 0
        ;
        OPEN  MY_ROWS_TYPE;
        FETCH NEXT FROM  MY_ROWS_TYPE INTO @ROW_ID_TYPE,@CURR_AP_ID_TYPE,@Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,@TYPE,@CURR_FEE,@CURR_COST;
        WHILE @@FETCH_STATUS = 0
        BEGIN
	        SELECT @CURR_AP_CHK_AMT_TYPE = SUM(ISNULL(AP_CHK_AMT,0))
			FROM AP_PMT_HIST APH    
			WHERE APH.AP_ID = @CURR_AP_ID_TYPE

			SELECT @CURR_AP_CHK_AMT_UNCLEARED_TYPE = SUM(ISNULL(AP_CHK_AMT,0))
			FROM AP_PMT_HIST APH INNER JOIN
                      CHECK_REGISTER ON APH.AP_CHK_NBR = CHECK_REGISTER.CHECK_NBR AND 
                      APH.CHK_SEQ = CHECK_REGISTER.CHK_SEQ  
			WHERE APH.AP_ID = @CURR_AP_ID_TYPE AND (CHECK_REGISTER.RECON_FLAG = 0 OR CHECK_REGISTER.RECON_FLAG IS NULL)

			IF @CURR_AP_CHK_AMT_TYPE > 0
			BEGIN
				IF @TYPE = 'Client Vendor Invoice'
				BEGIN
					INSERT INTO #CLIENT_BILLING_TYPE
					SELECT @Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,CASE WHEN @TYPE = 'Client Vendor Invoice' THEN 'Client Vendor Invoice Paid' ELSE '' END,
						0,CASE WHEN @TYPE = 'Client Vendor Invoice' THEN @CURR_COST ELSE 0 END,@CURR_AP_ID_TYPE
				END
				IF @TYPE = 'Non Billable Vendor Invoice'
				BEGIN
					INSERT INTO #CLIENT_BILLING_TYPE
					SELECT @Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,CASE WHEN @TYPE = 'Non Billable Vendor Invoice' THEN 'Non Billable Vendor Invoice Paid' ELSE '' END,
						0,CASE WHEN @TYPE = 'Non Billable Vendor Invoice' THEN @CURR_COST ELSE 0 END,@CURR_AP_ID_TYPE
				END	
			END		

			IF @CURR_AP_CHK_AMT_UNCLEARED_TYPE > 0
			BEGIN
				IF @TYPE = 'Client Vendor Invoice'
				BEGIN
					INSERT INTO #CLIENT_BILLING_TYPE
					SELECT @Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,CASE WHEN @TYPE = 'Client Vendor Invoice' THEN 'Client Vendor Invoice Paid Uncleared' ELSE '' END,
						0,CASE WHEN @TYPE = 'Client Vendor Invoice' THEN @CURR_COST ELSE 0 END,@CURR_AP_ID_TYPE
				END
				IF @TYPE = 'Non Billable Vendor Invoice'
				BEGIN
					INSERT INTO #CLIENT_BILLING_TYPE
					SELECT @Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,CASE WHEN @TYPE = 'Non Billable Vendor Invoice' THEN 'Non Billable Vendor Invoice Paid Uncleared' ELSE '' END,
						0,CASE WHEN @TYPE = 'Non Billable Vendor Invoice' THEN @CURR_COST ELSE 0 END,@CURR_AP_ID_TYPE
				END	
			END	
	        
			--GO TO NEXT EVENT
	        FETCH NEXT FROM  MY_ROWS_TYPE INTO @ROW_ID_TYPE,@CURR_AP_ID_TYPE,@Client,@ClientDesc,@Division,@DivisionDesc,@Product,@ProductDesc,@PP,@TYPE,@CURR_FEE,@CURR_COST;
        END
        CLOSE  MY_ROWS_TYPE;
        DEALLOCATE  MY_ROWS_TYPE;
