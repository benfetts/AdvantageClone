CREATE PROCEDURE [dbo].[advsp_media_broadcast_worksheet_load_estimate_ordered_lines]
	@MEDIA_BROADCAST_WORKSHEET_MARKET_ID int
AS
BEGIN

	SELECT
		DISTINCT
		[VendorCode] = MBWMD.VN_CODE,
		[LineNumber] = MBWMD.LINE_NUMBER,
		[MakegoodNumber] = MBWMD.MAKEGOOD_NUMBER,
		[MadegoodNumber] = MBWMD.MADEGOOD_NUMBER
	FROM 
		[dbo].[MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_DATE] MBWMDD
		INNER JOIN [dbo].[MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL] MBWMD ON MBWMD.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_ID = MBWMDD.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_ID 
	WHERE 
		MBWMD.MEDIA_BROADCAST_WORKSHEET_MARKET_ID = @MEDIA_BROADCAST_WORKSHEET_MARKET_ID
		AND MBWMDD.LINK_ID IS NOT NULL
		AND MBWMDD.ORDER_NBR IS NOT NULL

END
GO


