CREATE PROCEDURE [dbo].[advsp_media_broadcast_worksheet_market_order_status] 
	@MEDIA_BROADCAST_WORKSHEET_MARKET_ID int
AS
BEGIN
	
	SELECT 
		WorksheetMarketDetailDateID = MBW_MDD.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_DATE_ID,
		OrderStatus = MBW_MDD.MEDIA_BROADCAST_WORKSHEET_ORDER_STATUS_ID,
		Spots = MBW_MDD.SPOTS,
		RevisionNumber = MBW_MD.REVISION_NUMBER
	FROM 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_DATE MBW_MDD 
		INNER JOIN dbo.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL MBW_MD ON MBW_MD.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_ID = MBW_MDD.MEDIA_BROADCAST_WORKSHEET_MARKET_DETAIL_ID
	WHERE 
		MBW_MD.MEDIA_BROADCAST_WORKSHEET_MARKET_ID = @MEDIA_BROADCAST_WORKSHEET_MARKET_ID

END
GO