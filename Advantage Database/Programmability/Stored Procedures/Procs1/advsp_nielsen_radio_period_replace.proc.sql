CREATE PROCEDURE [dbo].[advsp_nielsen_radio_period_replace]
       @OldNielsenRadioPeriodID AS int,
       @NewNielsenRadioPeriodID AS int
AS
BEGIN

	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET 
	SET 
		NIELSEN_RADIO_PERIOD_ID1 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID1 = @OldNielsenRadioPeriodID
		
	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET 
	SET 
		NIELSEN_RADIO_PERIOD_ID2 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID2 = @OldNielsenRadioPeriodID
		
	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET 
	SET 
		NIELSEN_RADIO_PERIOD_ID3 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID3 = @OldNielsenRadioPeriodID

	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET 
	SET 
		NIELSEN_RADIO_PERIOD_ID4 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID4 = @OldNielsenRadioPeriodID

	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET 
	SET 
		NIELSEN_RADIO_PERIOD_ID5 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID5 = @OldNielsenRadioPeriodID

	UPDATE 
		dbo.MEDIA_BROADCAST_WORKSHEET_MARKET_NIELSEN_RADIO_PERIOD
	SET 
		NIELSEN_RADIO_PERIOD_ID = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID = @OldNielsenRadioPeriodID
		
	UPDATE 
		dbo.MEDIA_SPOT_RADIO_RESEARCH_BOOK 
	SET 
		NIELSEN_RADIO_PERIOD_ID1 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID1 = @OldNielsenRadioPeriodID
		
	UPDATE 
		dbo.MEDIA_SPOT_RADIO_RESEARCH_BOOK 
	SET 
		NIELSEN_RADIO_PERIOD_ID2 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID2 = @OldNielsenRadioPeriodID
		
	UPDATE 
		dbo.MEDIA_SPOT_RADIO_RESEARCH_BOOK 
	SET 
		NIELSEN_RADIO_PERIOD_ID3 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID3 = @OldNielsenRadioPeriodID

	UPDATE 
		dbo.MEDIA_SPOT_RADIO_RESEARCH_BOOK 
	SET 
		NIELSEN_RADIO_PERIOD_ID4 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID4 = @OldNielsenRadioPeriodID

	UPDATE 
		dbo.MEDIA_SPOT_RADIO_RESEARCH_BOOK 
	SET 
		NIELSEN_RADIO_PERIOD_ID5 = @NewNielsenRadioPeriodID 
	WHERE 
		NIELSEN_RADIO_PERIOD_ID5 = @OldNielsenRadioPeriodID

END
GO


