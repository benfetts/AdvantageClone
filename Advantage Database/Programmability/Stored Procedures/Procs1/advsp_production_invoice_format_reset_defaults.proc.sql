		[LOGO_PATH] = PID.[LOGO_PATH],
		[ADDRESS_BLOCK] = PID.[ADDRESS_BLOCK],
		[NAME_OVERIDE] = PID.[NAME_OVERIDE],
		[INV_REF_INCL] = PID.[INV_REF_INCL],
		[INV_USER_INCL] = PID.[INV_USER_INCL],
		[INV_CL_PO_INCL] = PID.[INV_CL_PO_INCL],
		[INV_AE_INCL] = PID.[INV_AE_INCL],
		[INV_CLASS_INCL] = PID.[INV_CLASS_INCL],
		[BILL_COMM_INV_INCL] = PID.[BILL_COMM_INV_INCL],
		[BILL_APPR_CL_COMM_INCL] = PID.[BILL_APPR_CL_COMM_INCL],
