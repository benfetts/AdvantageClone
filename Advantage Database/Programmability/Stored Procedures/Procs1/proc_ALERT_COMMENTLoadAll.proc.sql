--DROP PROCEDURE [dbo].[proc_ALERT_COMMENTLoadAll]
CREATE PROCEDURE [dbo].[proc_ALERT_COMMENTLoadAll]
AS
BEGIN

	SET NOCOUNT ON
	DECLARE @Err int

	SELECT
		[COMMENT_ID],
		[ALERT_ID],
		[USER_CODE],
		[GENERATED_DATE],
		[COMMENT],
		[EMAILSENT],
		[USER_CODE_CP],
		[DOCUMENT_LIST]
	FROM [ALERT_COMMENT]
	ORDER BY
		   GENERATED_DATE DESC,
		   COMMENT_ID DESC

	SET @Err = @@Error

	RETURN @Err

END