--DROP PROCEDURE [dbo].[proc_ALERT_COMMENTLoadByPrimaryKey]
CREATE PROCEDURE [dbo].[proc_ALERT_COMMENTLoadByPrimaryKey]
(
	@COMMENT_ID int
)
AS
BEGIN
	SET NOCOUNT ON
	DECLARE @Err int

	SELECT
		[COMMENT_ID],
		[ALERT_ID],
		[USER_CODE],
		[GENERATED_DATE],
		[COMMENT],
		[EMAILSENT],
		[USER_CODE_CP],
		[DOCUMENT_LIST]
	FROM [ALERT_COMMENT]
	WHERE
		([COMMENT_ID] = @COMMENT_ID)

	SET @Err = @@Error

	RETURN @Err

END