





CREATE PROCEDURE [dbo].[proc_CP_USER_QUICK_LAUNCH_APPSUpdate]
(
	@CDP_CONTACT_ID int,
	@TAB_ID int,
	@APPLICATION_ID int
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	UPDATE [CP_USER_QUICK_LAUNCH_APPS]
	SET
		[TAB_ID] = @TAB_ID,
		[APPLICATION_ID] = @APPLICATION_ID
	WHERE
		[CDP_CONTACT_ID] = @CDP_CONTACT_ID


	SET @Err = @@Error


	RETURN @Err
END




