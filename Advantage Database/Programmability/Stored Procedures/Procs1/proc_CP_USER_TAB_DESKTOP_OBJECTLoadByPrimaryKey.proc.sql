


CREATE PROCEDURE [dbo].[proc_CP_USER_TAB_DESKTOP_OBJECTLoadByPrimaryKey]
(
	@DESKTOP_OBJECT_ID int,
	@TAB_ID int,
	@CDP_CONTACT_ID int
)
AS
BEGIN
	SET NOCOUNT ON
	DECLARE @Err int

	SELECT
		[CDP_CONTACT_ID],
		[TAB_ID],
		[DESKTOP_OBJECT_ID],
		[PARENT_ZONE],
		[LOAD_INDEX]
	FROM [CP_USER_TAB_DESKTOP_OBJECT]
	WHERE
		([DESKTOP_OBJECT_ID] = @DESKTOP_OBJECT_ID) AND
		([TAB_ID] = @TAB_ID) AND
		([CDP_CONTACT_ID] = @CDP_CONTACT_ID)

	SET @Err = @@Error

	RETURN @Err
END





