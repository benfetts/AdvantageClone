


CREATE PROCEDURE [dbo].[proc_CP_USER_TAB_DESKTOP_OBJECTUpdate]
(
	@CDP_CONTACT_ID int,
	@TAB_ID int,
	@DESKTOP_OBJECT_ID int,
	@PARENT_ZONE varchar(50) = NULL,
	@LOAD_INDEX int = NULL
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	UPDATE [CP_USER_TAB_DESKTOP_OBJECT]
	SET
		[PARENT_ZONE] = @PARENT_ZONE,
		[LOAD_INDEX] = @LOAD_INDEX
	WHERE
		[DESKTOP_OBJECT_ID] = @DESKTOP_OBJECT_ID
	AND	[TAB_ID] = @TAB_ID
	AND	[CDP_CONTACT_ID] = @CDP_CONTACT_ID


	SET @Err = @@Error


	RETURN @Err
END





