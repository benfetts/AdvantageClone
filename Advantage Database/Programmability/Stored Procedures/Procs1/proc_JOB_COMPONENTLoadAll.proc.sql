		[JOB_COMPONENT_NBR],
		[JOB_BILL_HOLD],
		[EMP_CODE],
		[JOB_COMP_DATE],
		[JOB_FIRST_USE_DATE],
		[START_DATE],
		[JOB_AD_SIZE],
		[JOB_SPEC_TYPE],
		[DP_TM_CODE],
		[JOB_MARKUP_PCT],
		[CREATIVE_INSTR],
		[JOB_LAYOUT_THUMB],
		[JOB_LAYOUT_ROUGH],
		[JOB_LAYOUT_COMP],
		[JOB_LAYOUT_NONE],
		[JOB_LAYOUT_SPECIAL],
		[JOB_LAYOUT_SPC_EXP],
		[JOB_PROCESS_CONTRL],
		[JOB_COMP_DESC],
		[JOB_COMP_COMMENTS],
		[JOB_COMP_BUDGET_AM],
		[ESTIMATE_NUMBER],
		[EST_COMPONENT_NBR],
		[BILLING_USER],
		[AB_FLAG],
		[JOB_DEL_INSTRUCT],
		[JT_CODE],
		[TAX_FLAG],
