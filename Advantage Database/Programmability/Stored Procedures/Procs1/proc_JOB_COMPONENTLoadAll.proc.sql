		[UDV2_CODE],
		[UDV3_CODE],
		[UDV4_CODE],
		[UDV5_CODE],
		[JOB_TMPLT_CODE],
		[FEE_TIME],
		[FISCAL_PERIOD_CODE],
		[JOB_QTY],
		[BLACKPLT_VER_CODE],
		[BLACKPLT_VER_DESC],
		[BLACKPLT_VER2_CODE],
		[BLACKPLT_VER2_DESC],
		ISNULL([CDP_CONTACT_ID],0) AS CDP_CONTACT_ID,
		[PRD_CONT_CODE]
	FROM [JOB_COMPONENT]

	SET @Err = @@Error

	RETURN @Err
END

