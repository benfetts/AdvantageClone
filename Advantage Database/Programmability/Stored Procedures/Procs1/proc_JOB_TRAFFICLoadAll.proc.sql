

CREATE PROCEDURE [dbo].[proc_JOB_TRAFFICLoadAll]
AS
BEGIN

	SET NOCOUNT ON
	DECLARE @Err int

	SELECT
		[JOB_NUMBER],
		[JOB_COMPONENT_NBR],
		[TRF_CODE],
		[TRF_PRESET_CODE],
		[TRF_COMMENTS],
		[ASSIGN_1],
		[ASSIGN_2],
		[ASSIGN_3],
		[ASSIGN_4],
		[ASSIGN_5],
		[COMPLETED_DATE],
		[DATE_DELIVERED],
		[DATE_SHIPPED],
		[RECEIVED_BY],
		[REFERENCE],
		[ROWID]
	FROM [JOB_TRAFFIC]

	SET @Err = @@Error

	RETURN @Err
END

