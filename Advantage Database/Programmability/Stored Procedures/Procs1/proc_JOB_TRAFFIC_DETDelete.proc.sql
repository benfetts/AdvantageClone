

CREATE PROCEDURE [dbo].[proc_JOB_TRAFFIC_DETDelete]
(
	@JOB_COMPONENT_NBR smallint,
	@JOB_NUMBER int,
	@SEQ_NBR smallint
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	DELETE
	FROM [JOB_TRAFFIC_DET]
	WHERE
		[JOB_COMPONENT_NBR] = @JOB_COMPONENT_NBR AND
		[JOB_NUMBER] = @JOB_NUMBER AND
		[SEQ_NBR] = @SEQ_NBR
	SET @Err = @@Error

	RETURN @Err
END

