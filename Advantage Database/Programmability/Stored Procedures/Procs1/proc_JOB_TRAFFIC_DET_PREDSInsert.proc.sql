

CREATE PROCEDURE [proc_JOB_TRAFFIC_DET_PREDSInsert]
(
	@ID int = NULL output,
	@JOB_NUMBER int,
	@JOB_COMPONENT_NBR int,
	@SEQ_NBR smallint,
	@PREDECESSOR_SEQ_NBR smallint
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	INSERT
	INTO [JOB_TRAFFIC_DET_PREDS]
	(
		[JOB_NUMBER],
		[JOB_COMPONENT_NBR],
		[SEQ_NBR],
		[PREDECESSOR_SEQ_NBR]
	)
	VALUES
	(
		@JOB_NUMBER,
		@JOB_COMPONENT_NBR,
		@SEQ_NBR,
		@PREDECESSOR_SEQ_NBR
	)

	SET @Err = @@Error

	SELECT @ID = SCOPE_IDENTITY()

	RETURN @Err
END

