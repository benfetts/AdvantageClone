

CREATE PROCEDURE [proc_JOB_TRAFFIC_DET_PREDSUpdate]
(
	@ID int,
	@JOB_NUMBER int,
	@JOB_COMPONENT_NBR int,
	@SEQ_NBR smallint,
	@PREDECESSOR_SEQ_NBR smallint
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	UPDATE [JOB_TRAFFIC_DET_PREDS]
	SET
		[JOB_NUMBER] = @JOB_NUMBER,
		[JOB_COMPONENT_NBR] = @JOB_COMPONENT_NBR,
		[SEQ_NBR] = @SEQ_NBR,
		[PREDECESSOR_SEQ_NBR] = @PREDECESSOR_SEQ_NBR
	WHERE
		[ID] = @ID


	SET @Err = @@Error


	RETURN @Err
END

