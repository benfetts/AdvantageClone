



























CREATE PROCEDURE [dbo].[proc_OFFICEInsert]
(
	@OFFICE_CODE varchar(4),
	@OFFICE_NAME varchar(30) = NULL,
	@GLACODE_AR varchar(30) = NULL,
	@GLACODE_AP varchar(30) = NULL,
	@GLACODE_AP_DISC varchar(30) = NULL,
	@PGLACODE_SALES varchar(30) = NULL,
	@PGLACODE_COS varchar(30) = NULL,
	@PGLACODE_WIP varchar(30) = NULL,
	@PGLACODE_DEF_SALES varchar(30) = NULL,
	@PGLACODE_DEF_COS varchar(30) = NULL,
	@PGLACODE_ACC_COS varchar(30) = NULL,
	@PGLACODE_ACC_AP varchar(30) = NULL,
	@PGLACODE_ACC_TAX varchar(30) = NULL,
	@MGLACODE_ACC_AP varchar(30) = NULL,
	@MGLACODE_ACC_COS varchar(30) = NULL,
	@MGLACODE_COS varchar(30) = NULL,
	@MGLACODE_DEF_COS varchar(30) = NULL,
	@MGLACODE_DEF_SALES varchar(30) = NULL,
	@MGLACODE_SALES varchar(30) = NULL,
	@MGLACODE_WIP varchar(30) = NULL,
	@MGLACODE_ACC_TAX varchar(30) = NULL,
	@GLACODE_SUSPENSE varchar(30) = NULL,
	@GLACODE_STATE varchar(30) = NULL,
	@GLACODE_COUNTY varchar(30) = NULL,
	@GLACODE_CITY varchar(30) = NULL,
	@PRD_AB_INCOME smallint = NULL,
	@MED_AB_INCOME smallint = NULL,
	@GLACODE_VOL_DISC varchar(30) = NULL,
	@GLACODE_AP_WH varchar(30) = NULL,
	@INACTIVE_FLAG smallint = NULL
)
AS
BEGIN

	SET NOCOUNT OFF
	DECLARE @Err int

	INSERT
	INTO [OFFICE]
	(
		[OFFICE_CODE],
		[OFFICE_NAME],
		[GLACODE_AR],
		[GLACODE_AP],
		[GLACODE_AP_DISC],
		[PGLACODE_SALES],
		[PGLACODE_COS],
		[PGLACODE_WIP],
		[PGLACODE_DEF_SALES],
		[PGLACODE_DEF_COS],
		[PGLACODE_ACC_COS],
		[PGLACODE_ACC_AP],
		[PGLACODE_ACC_TAX],
		[MGLACODE_ACC_AP],
		[MGLACODE_ACC_COS],
		[MGLACODE_COS],
		[MGLACODE_DEF_COS],
		[MGLACODE_DEF_SALES],
		[MGLACODE_SALES],
		[MGLACODE_WIP],
		[MGLACODE_ACC_TAX],
		[GLACODE_SUSPENSE],
		[GLACODE_STATE],
		[GLACODE_COUNTY],
		[GLACODE_CITY],
		[PRD_AB_INCOME],
		[MED_AB_INCOME],
		[GLACODE_VOL_DISC],
		[GLACODE_AP_WH],
		[INACTIVE_FLAG]
	)
	VALUES
	(
		@OFFICE_CODE,
		@OFFICE_NAME,
		@GLACODE_AR,
		@GLACODE_AP,
		@GLACODE_AP_DISC,
		@PGLACODE_SALES,
		@PGLACODE_COS,
		@PGLACODE_WIP,
		@PGLACODE_DEF_SALES,
		@PGLACODE_DEF_COS,
		@PGLACODE_ACC_COS,
		@PGLACODE_ACC_AP,
		@PGLACODE_ACC_TAX,
		@MGLACODE_ACC_AP,
		@MGLACODE_ACC_COS,
		@MGLACODE_COS,
		@MGLACODE_DEF_COS,
		@MGLACODE_DEF_SALES,
		@MGLACODE_SALES,
		@MGLACODE_WIP,
		@MGLACODE_ACC_TAX,
		@GLACODE_SUSPENSE,
		@GLACODE_STATE,
		@GLACODE_COUNTY,
		@GLACODE_CITY,
		@PRD_AB_INCOME,
		@MED_AB_INCOME,
		@GLACODE_VOL_DISC,
		@GLACODE_AP_WH,
		@INACTIVE_FLAG
	)

	SET @Err = @@Error


	RETURN @Err
END



























