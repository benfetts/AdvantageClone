





CREATE PROCEDURE [dbo].[proc_WV_PO_GetPOApprData] 
@POEmpCode varchar(6),
@PONum int
AS

SELECT     PO_APPROVAL.PO_NUMBER, PO_APPROVAL.PO_APPR_RULE_CODE, PO_APPROVAL.SEQ_NBR, 
                      PO_APPROVAL.PO_APPR_RULE_ID, PO_APPR_RULE_EMP.EMP_CODE, ISNULL(PO_APPROVAL.PO_APPROVAL_NOTES,'') as PO_APPROVAL_NOTES,
					  PO_APPROVAL.PO_APPROVAL_FLAG
FROM         PO_APPROVAL INNER JOIN
                      PO_APPR_RULE_EMP ON PO_APPROVAL.PO_APPR_RULE_ID = PO_APPR_RULE_EMP.PO_APPR_RULE_ID
WHERE     (PO_APPR_RULE_EMP.EMP_CODE = @POEmpCode) AND (PO_APPROVAL.PO_NUMBER = @PONum)
























