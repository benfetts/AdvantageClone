
CREATE PROCEDURE [dbo].[usp_wv_ALERT_NOTIFY_HDR_INSERT] /*WITH ENCRYPTION*/
@ALERT_NOTIFY_NAME VARCHAR(100),
@ACTIVE_FLAG BIT,
@TYPE SMALLINT,
@AUTO_NXT_STATE BIT
AS
/*=========== QUERY ===========*/
	DECLARE @NEW_ID INT;
	INSERT INTO ALERT_NOTIFY_HDR
	  (
		ALERT_NOTIFY_NAME,
		ACTIVE_FLAG,
		[TYPE],
		AUTO_NXT_STATE
	  )
	VALUES
	  (
		@ALERT_NOTIFY_NAME,
		@ACTIVE_FLAG,
		@TYPE,
		@AUTO_NXT_STATE
	  );
	SELECT @NEW_ID = SCOPE_IDENTITY();
	SELECT @NEW_ID;
/*=========== QUERY ===========*/
