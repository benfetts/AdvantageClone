
CREATE PROCEDURE [dbo].[usp_wv_ALERT_STATES_UPDATE] /*WITH ENCRYPTION*/
	@ALERT_STATE_ID INT,
	@ALERT_STATE_NAME VARCHAR(100),
	@ACTIVE_FLAG BIT,
	@DFLT_ALERT_CAT_ID INT
AS
/*=========== QUERY ===========*/
	UPDATE
		ALERT_STATES WITH ( ROWLOCK )
	SET 
		ALERT_STATE_NAME = @ALERT_STATE_NAME,
		ACTIVE_FLAG = @ACTIVE_FLAG,
		DFLT_ALERT_CAT_ID = @DFLT_ALERT_CAT_ID
	WHERE
		ALERT_STATES.ALERT_STATE_ID = @ALERT_STATE_ID ;
/*=========== QUERY ===========*/
