
CREATE PROCEDURE [dbo].[usp_wv_BA_APPROVAL_JC_CLEAR_APPROVAL_AMTS]
	@BA_ID AS INT,
	@JOB_NUMBER AS INT,
	@JOB_COMPONENT_NBR AS SMALLINT
AS

		UPDATE 
			BILL_APPR_DTL WITH(ROWLOCK) 
		SET 
			APPROVED_AMT = NULL
		WHERE 
			BA_ID = @BA_ID 
			AND JOB_NUMBER = @JOB_NUMBER 
			AND JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR;
	
