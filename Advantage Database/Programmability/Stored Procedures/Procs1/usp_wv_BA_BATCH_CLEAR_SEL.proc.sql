
CREATE PROCEDURE [dbo].[usp_wv_BA_BATCH_CLEAR_SEL] 
@BA_BATCH_ID INT,
@FINISH_BATCH SMALLINT

AS

	UPDATE 
		JOB_COMPONENT WITH(ROWLOCK) 
	SET 
		BA_BATCH_ID = NULL
	WHERE 
		BA_BATCH_ID = @BA_BATCH_ID;

	IF @FINISH_BATCH = 1
	BEGIN
		UPDATE
			BILL_APPR_BATCH WITH(ROWLOCK)
		SET
			FINISHED = 1
		WHERE
			BA_BATCH_ID = @BA_BATCH_ID;
	END
		

