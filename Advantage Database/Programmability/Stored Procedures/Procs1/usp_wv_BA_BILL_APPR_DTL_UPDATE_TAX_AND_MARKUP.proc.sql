
CREATE PROCEDURE [dbo].[usp_wv_BA_BILL_APPR_DTL_UPDATE_TAX_AND_MARKUP] 
	@BA_DTL_ID AS INTEGER,
	@APPR_RESALE_TAX AS DECIMAL(14,2),
	@APPR_VENDOR_TAX AS DECIMAL(14,2),
	@APPR_MARKUP_AMT AS DECIMAL(14,2)
AS


	UPDATE BILL_APPR_DTL WITH(ROWLOCK)
	SET
		APPR_RESALE_TAX = @APPR_RESALE_TAX,
		APPR_VENDOR_TAX = @APPR_VENDOR_TAX,
		APPR_MARKUP_AMT = @APPR_MARKUP_AMT
	WHERE
		BA_DTL_ID = @BA_DTL_ID;
		
		
