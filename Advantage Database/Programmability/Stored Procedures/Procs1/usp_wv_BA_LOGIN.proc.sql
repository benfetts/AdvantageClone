
CREATE PROCEDURE [dbo].[usp_wv_BA_LOGIN]
	@USER_ID VARCHAR(100),
	@AE_EMP_CODE VARCHAR(6),
	@PASSWORD VARCHAR(20)

AS


	SELECT     
		ISNULL(COUNT(1),0)
	FROM         
		APPR_PASSWORD WITH(NOLOCK)
	WHERE     
		((INACTIVE IS NULL) OR (INACTIVE = 0))
		AND (ACCT_EXEC = @AE_EMP_CODE) 
		AND (PASSWORD_ID = @PASSWORD);
		
		
