





CREATE PROCEDURE [dbo].[usp_wv_calendar_ddMediaDelivery]
@VendorCode varchar(50),
@Type varchar(2)
AS


SELECT     VENDOR.SHIP_ADDR1, VENDOR.SHIP_ADDR2, VENDOR.SHIP_ADDR3, VENDOR.SHIP_CITY, VENDOR.SHIP_STATE, 
                      VENDOR.SHIP_ZIP, VENDOR.SHIP_COUNTY, VENDOR.SHIP_COUNTRY, VENDOR_COMMENTS.DLVRY_GEN_INFO, VENDOR_COMMENTS.ACCEPTED_MEDIA, 
                      VENDOR_COMMENTS.EFILE_INFO, VENDOR_COMMENTS.PREF_MATL, VENDOR_COMMENTS.FTP_USER, 
                      VENDOR_COMMENTS.FTP_PW, VENDOR_COMMENTS.FTP_DIR
FROM         VENDOR LEFT OUTER JOIN
                      VENDOR_COMMENTS ON VENDOR.VN_CODE = VENDOR_COMMENTS.VN_CODE
WHERE       VENDOR.VN_CODE = @VendorCode                                                  




