IF EXISTS (SELECT * FROM dbo.sysobjects WHERE id = object_id(N'[dbo].[usp_wv_ALERT_GET_MENTIONS]') AND OBJECTPROPERTY(id, N'IsProcedure') = 1)
    DROP PROCEDURE [dbo].[usp_wv_ALERT_GET_MENTIONS]
GO
CREATE PROCEDURE [dbo].[usp_wv_ALERT_GET_MENTIONS]

@ALERT_ID INT,
@USER_CODE VARCHAR(6)

AS

DECLARE @COMMENT_ID INT = 0

--BEGIN
--	SELECT @COMMENT_ID = MAX(COMMENT_ID) FROM ALERT_COMMENT WITH (NOLOCK)
--END

BEGIN
	SELECT DISTINCT MENTION_EMP_CODE AS EMPLOYEE_CODE FROM ALERT_MENTION WITH (NOLOCK)
	WHERE ALERT_ID = @ALERT_ID
	AND COMMENT_ID = @COMMENT_ID
	AND USER_CODE = @USER_CODE
END