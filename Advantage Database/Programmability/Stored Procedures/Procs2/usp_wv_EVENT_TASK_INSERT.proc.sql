
CREATE PROCEDURE [dbo].[usp_wv_EVENT_TASK_INSERT] 
	@EVENT_ID AS INT,
	@TASK_CODE AS VARCHAR(10),
	@EMP_CODE AS VARCHAR(6),
	@START_DATE AS SMALLDATETIME,
	@END_DATE AS SMALLDATETIME,
	@START_TIME AS SMALLDATETIME,
	@END_TIME AS SMALLDATETIME,
	@TEMP_COMP_DATE AS SMALLDATETIME,
	@HOURS_ALLOWED AS DECIMAL(18,2),
	@COMMENTS AS TEXT,
	@COMPLETED_COMMENTS AS TEXT
AS

    INSERT INTO EVENT_TASK WITH(ROWLOCK)
    (
	    EVENT_ID,
	    TASK_CODE,
	    EMP_CODE,
	    START_DATE,
	    END_DATE,
	    START_TIME,
	    END_TIME,
	    TEMP_COMP_DATE,
	    HOURS_ALLOWED,
	    COMMENTS,
	    COMPLETED_COMMENTS
    )
    VALUES
    (
	    @EVENT_ID,
	    @TASK_CODE,
	    @EMP_CODE,
	    @START_DATE,
	    @END_DATE,
	    @START_TIME,
	    @END_TIME,
	    @TEMP_COMP_DATE,
	    @HOURS_ALLOWED,
	    @COMMENTS,
	    @COMPLETED_COMMENTS
    );


