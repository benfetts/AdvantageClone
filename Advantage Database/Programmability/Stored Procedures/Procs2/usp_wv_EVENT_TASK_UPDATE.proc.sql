
CREATE PROCEDURE [dbo].[usp_wv_EVENT_TASK_UPDATE] 
	@EVENT_TASK_ID AS INT,
	@EVENT_ID AS INT,
	@TASK_CODE AS VARCHAR(10),
	@EMP_CODE AS VARCHAR(6),
	@START_DATE AS SMALLDATETIME,
	@END_DATE AS SMALLDATETIME,
	@START_TIME AS SMALLDATETIME,
	@END_TIME AS SMALLDATETIME,
	@TEMP_COMP_DATE AS SMALLDATETIME,
	@HOURS_ALLOWED AS DECIMAL(18,2),
	@COMMENTS AS TEXT,
	@COMPLETED_COMMENTS AS TEXT

AS


    UPDATE EVENT_TASK WITH(ROWLOCK)
    SET
	    EVENT_ID = @EVENT_ID,
	    TASK_CODE = @TASK_CODE,
	    EMP_CODE = @EMP_CODE,
	    [START_DATE] = @START_DATE,
	    END_DATE = @END_DATE,
	    START_TIME = @START_TIME,
	    END_TIME = @END_TIME,
	    TEMP_COMP_DATE = @TEMP_COMP_DATE,
	    HOURS_ALLOWED = @HOURS_ALLOWED,
	    COMMENTS = @COMMENTS,
	    COMPLETED_COMMENTS = @COMPLETED_COMMENTS
    WHERE
	    EVENT_TASK_ID = @EVENT_TASK_ID;	
	    
	    
