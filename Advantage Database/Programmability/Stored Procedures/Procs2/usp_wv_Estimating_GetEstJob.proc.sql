
CREATE PROCEDURE [dbo].[usp_wv_Estimating_GetEstJob] 
@JOB_NUMBER INT,
@JOB_COMPONENT_NBR SMALLINT

AS


SELECT ISNULL(ESTIMATE_NUMBER,0) AS ESTIMATE_NUMBER, ISNULL(EST_COMPONENT_NBR,0) AS EST_COMPONENT_NBR
FROM JOB_COMPONENT
WHERE JOB_NUMBER =  @JOB_NUMBER AND JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR



