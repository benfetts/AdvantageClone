





CREATE PROCEDURE [dbo].[usp_wv_Estimating_GetInternalApprovalByQuote] 
@ESTIMATE_NUMBER INT,
@EST_COMPONENT_NBR INT,
@EST_QUOTE_NBR INT
AS


SELECT     EST_QUOTE_NBR, EST_REVISION_NBR, EST_APPR_BY, EST_APPR_DATE, CREATE_USER, CREATE_DATE, ISNULL(APPR_NOTES,'') AS APPR_NOTES
FROM         ESTIMATE_INT_APPR
WHERE     (ESTIMATE_NUMBER = @ESTIMATE_NUMBER) AND (EST_COMPONENT_NBR = @EST_COMPONENT_NBR) AND (EST_QUOTE_NBR = @EST_QUOTE_NBR)
























