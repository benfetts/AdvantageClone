






















CREATE PROCEDURE [dbo].[usp_wv_Estimating_JobSpecs] 
@EstNo Int,
@EstComp Int,
@JobNo Int,
@JobComp Int

AS

SELECT     JOB_SPECS.SPEC_VER, JOB_SPECS.SPEC_VER_DESC, JOB_SPECS.SPEC_REV, JOB_SPEC_QTY.JOB_QTY, JOB_SPEC_QTY.SEQ_NBR
FROM         JOB_LOG INNER JOIN
                      JOB_COMPONENT ON JOB_LOG.JOB_NUMBER = JOB_COMPONENT.JOB_NUMBER INNER JOIN
                      JOB_SPECS ON JOB_COMPONENT.JOB_NUMBER = JOB_SPECS.JOB_NUMBER AND 
                      JOB_COMPONENT.JOB_COMPONENT_NBR = JOB_SPECS.JOB_COMPONENT_NBR LEFT OUTER JOIN
                      JOB_SPEC_QTY ON JOB_SPECS.JOB_NUMBER = JOB_SPEC_QTY.JOB_NUMBER AND 
                      JOB_SPECS.JOB_COMPONENT_NBR = JOB_SPEC_QTY.JOB_COMPONENT_NBR AND 
                      JOB_SPECS.SPEC_VER = JOB_SPEC_QTY.SPEC_VER AND JOB_SPECS.SPEC_REV = JOB_SPEC_QTY.SPEC_REV
WHERE JOB_COMPONENT.ESTIMATE_NUMBER = @EstNo AND JOB_COMPONENT.EST_COMPONENT_NBR = @EstComp AND
		JOB_SPECS.JOB_NUMBER = @JobNo AND JOB_SPECS.JOB_COMPONENT_NBR = @JobComp
ORDER BY JOB_SPECS.SPEC_VER, JOB_SPECS.SPEC_REV






















