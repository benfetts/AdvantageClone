







CREATE PROCEDURE [dbo].[usp_wv_GetAdNumBlackPlt]
@AdNum varchar(30)
AS

SELECT     AD_NUMBER.AD_NBR, ISNULL(AD_NUMBER.AD_NBR_DESC, '') AS AD_NBR_DESC, ISNULL(AD_NUMBER.DEF_BLKPLT_VER_CODE, '') AS DEF_BLKPLT_VER_CODE, 
                      ISNULL(BLACKPLT_VERSION.BLACKPLT_VER_DESC, '') AS BLACKPLT_VER_DESC, ISNULL(AD_NUMBER.DEF_BLKPLT_VER2_CODE, '') AS DEF_BLKPLT_VER2_CODE, 
                      ISNULL(BLACKPLT_VERSION_1.BLACKPLT_VER_DESC, '') AS BLACKPLT_VER2_DESC
FROM         AD_NUMBER LEFT OUTER JOIN
                      BLACKPLT_VERSION AS BLACKPLT_VERSION_1 ON 
                      AD_NUMBER.DEF_BLKPLT_VER2_CODE = BLACKPLT_VERSION_1.BLACKPLT_VER_CODE LEFT OUTER JOIN
                      BLACKPLT_VERSION ON AD_NUMBER.DEF_BLKPLT_VER_CODE = BLACKPLT_VERSION.BLACKPLT_VER_CODE
WHERE     (AD_NUMBER.AD_NBR = @AdNum)  




















