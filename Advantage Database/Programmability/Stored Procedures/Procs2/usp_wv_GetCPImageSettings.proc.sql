

CREATE PROCEDURE [dbo].[usp_wv_GetCPImageSettings]
	@BINARY_IMAGE_CLIENT varchar(6)
AS

if @BINARY_IMAGE_CLIENT <> ''
Begin
	 SELECT *
	 FROM   CP_BINARY_IMAGES WITH(NOLOCK)
	 WHERE  BINARY_IMAGE_CLIENT = @BINARY_IMAGE_CLIENT
End
Else
Begin
	 SELECT *
	 FROM   CP_BINARY_IMAGES WITH(NOLOCK)
End
 
 

