

CREATE PROCEDURE [dbo].[usp_wv_GetUserRights] 
@USER_ID AS varchar(100),
@APP_ID AS varchar(100)


AS

SET NOCOUNT ON

SELECT     USER_ID, ISNULL(SEC_VIEW,'N') AS SEC_VIEW, ISNULL(SEC_EDIT,'N') AS SEC_EDIT, ISNULL(SEC_INSERT,'N') AS SEC_INSERT
FROM         SEC_RIGHTS
WHERE     UPPER(USER_ID) = UPPER(@USER_ID) AND APPLICATION_ID = @APP_ID

