
CREATE PROCEDURE [dbo].[usp_wv_Job_Template_ContactDPList] 
@Client varchar(6)
AS

Set NoCount On

SELECT     DIVISION.DIV_CODE, DIVISION.DIV_NAME, '' AS PRD_CODE, '' AS PRD_DESCRIPTION
FROM         DIVISION 
WHERE     (DIVISION.CL_CODE = @Client)	
UNION
SELECT     DIVISION.DIV_CODE, DIVISION.DIV_NAME, PRODUCT.PRD_CODE, PRODUCT.PRD_DESCRIPTION
FROM         PRODUCT INNER JOIN
                      DIVISION ON PRODUCT.CL_CODE = DIVISION.CL_CODE AND PRODUCT.DIV_CODE = DIVISION.DIV_CODE
WHERE     (PRODUCT.CL_CODE = @Client)	
	
