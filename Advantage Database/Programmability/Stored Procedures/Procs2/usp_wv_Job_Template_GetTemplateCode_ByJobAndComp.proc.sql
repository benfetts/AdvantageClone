

CREATE PROCEDURE [dbo].[usp_wv_Job_Template_GetTemplateCode_ByJobAndComp] 
	@JOB_NUMBER INT,
	@JOB_COMPONENT_NBR INT
AS
DECLARE 
@TMPLT_CODE varchar(6)

	SELECT 
		@TMPLT_CODE = ISNULL(JOB_TMPLT_CODE,'DFLT') 
	FROM 
		JOB_COMPONENT 
	WHERE 
		JOB_NUMBER=@JOB_NUMBER 
		AND JOB_COMPONENT_NBR=@JOB_COMPONENT_NBR

	SELECT ISNULL(@TMPLT_CODE,'DFLT')

