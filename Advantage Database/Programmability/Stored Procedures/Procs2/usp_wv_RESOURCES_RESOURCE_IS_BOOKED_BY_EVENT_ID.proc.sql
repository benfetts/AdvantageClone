
CREATE PROCEDURE [dbo].[usp_wv_RESOURCES_RESOURCE_IS_BOOKED_BY_EVENT_ID] /*WITH ENCRYPTION*/
@RESOURCE_CODE      VARCHAR(6),
@EVENT_ID INT
AS
/*=========== QUERY ===========*/
	DECLARE
		@EVENT_DATE  SMALLDATETIME,
		@START_TIME  SMALLDATETIME,
		@END_TIME    SMALLDATETIME

	SELECT @EVENT_DATE = EVENT_DATE, @START_TIME = START_TIME, @END_TIME = END_TIME FROM [EVENT] WITH(NOLOCK)
	WHERE EVENT_ID = @EVENT_ID;

	EXEC usp_wv_RESOURCES_RESOURCE_IS_BOOKED @RESOURCE_CODE, @EVENT_ID, @EVENT_DATE, @START_TIME, @END_TIME,0;
/*=========== QUERY ===========*/
