
CREATE PROCEDURE [dbo].[usp_wv_Task_GetEmpComment] 
@JOB_NUMBER INT, 
@JOB_COMPONENT_NBR SMALLINT, 
@SEQ_NBR SMALLINT, 
@EMP_CODE VARCHAR(6)

AS


SELECT     
	ISNULL(COMPLETED_COMMENTS,'') AS COMPLETED_COMMENTS
FROM         
	JOB_TRAFFIC_DET_EMPS WITH(NOLOCK)
WHERE     
	(JOB_NUMBER = @JOB_NUMBER) 
	AND (JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR) 
	AND (SEQ_NBR = @SEQ_NBR) 
	AND (EMP_CODE =@EMP_CODE)
