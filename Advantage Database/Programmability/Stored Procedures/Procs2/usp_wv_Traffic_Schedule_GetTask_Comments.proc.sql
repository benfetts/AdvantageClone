

CREATE PROCEDURE [dbo].[usp_wv_Traffic_Schedule_GetTask_Comments]
@JOB_NUMBER INT,
@JOB_COMPONENT_NBR INT,
@SEQ SMALLINT
AS

    SELECT     
        ISNULL(JOB_TRAFFIC_DET.FNC_COMMENTS,'') AS FNC_COMMENTS, 
        ISNULL(JOB_TRAFFIC_DET.DUE_DATE_COMMENTS,'') AS DUE_DATE_COMMENTS, 
        ISNULL(JOB_TRAFFIC_DET.REV_DATE_COMMENTS,'') AS REV_DATE_COMMENTS, 
        ISNULL(JOB_TRAFFIC.TRF_COMMENTS,'') AS TRF_COMMENTS
    FROM         
        JOB_TRAFFIC_DET INNER JOIN
        JOB_TRAFFIC ON JOB_TRAFFIC_DET.JOB_NUMBER = JOB_TRAFFIC.JOB_NUMBER AND 
        JOB_TRAFFIC_DET.JOB_COMPONENT_NBR = JOB_TRAFFIC.JOB_COMPONENT_NBR
    WHERE     
	 (JOB_TRAFFIC_DET.JOB_NUMBER = @JOB_NUMBER) AND (JOB_TRAFFIC_DET.JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR) AND (JOB_TRAFFIC_DET.SEQ_NBR = @SEQ)



