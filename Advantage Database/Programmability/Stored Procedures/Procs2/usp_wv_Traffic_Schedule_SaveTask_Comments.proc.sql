

CREATE PROCEDURE [dbo].[usp_wv_Traffic_Schedule_SaveTask_Comments]
@JOB_NUMBER INT,
@JOB_COMPONENT_NBR INT,
@SEQ SMALLINT,
@TaskComment NTEXT,
@DueDateComment NTEXT,
@RevisionDateComment NTEXT
AS

    UPDATE 
        JOB_TRAFFIC_DET WITH(ROWLOCK)
    SET
        JOB_TRAFFIC_DET.FNC_COMMENTS = @TaskComment,
        JOB_TRAFFIC_DET.DUE_DATE_COMMENTS = @DueDateComment, 
        JOB_TRAFFIC_DET.REV_DATE_COMMENTS = @RevisionDateComment
    WHERE 
	 (JOB_TRAFFIC_DET.JOB_NUMBER = @JOB_NUMBER) AND (JOB_TRAFFIC_DET.JOB_COMPONENT_NBR = @JOB_COMPONENT_NBR) AND (JOB_TRAFFIC_DET.SEQ_NBR = @SEQ)



