


CREATE PROCEDURE [dbo].[usp_wv_Traffic_Schedule_Save_UserColumns_ShowFullColumnText] 
@HDR_CODE VARCHAR(6),
@COLUMN_ID INT
AS
DECLARE
@CURRENT_VALUE INT

SELECT     @CURRENT_VALUE = ISNULL(SHOW_LONG_DESC,0)
FROM         JOB_TRAFFIC_SETUP_DTL WITH(NOLOCK)
WHERE     (HDR_CODE = @HDR_CODE) AND (COLUMN_ID = @COLUMN_ID)

 IF @CURRENT_VALUE = 0
    BEGIN
        UPDATE JOB_TRAFFIC_SETUP_DTL WITH(ROWLOCK)
        SET SHOW_LONG_DESC = 1
        WHERE     (HDR_CODE = @HDR_CODE) AND (COLUMN_ID = @COLUMN_ID)
    END
ELSE
    BEGIN
        UPDATE JOB_TRAFFIC_SETUP_DTL WITH(ROWLOCK)
        SET SHOW_LONG_DESC = 0
        WHERE     (HDR_CODE = @HDR_CODE) AND (COLUMN_ID = @COLUMN_ID)
    END 
