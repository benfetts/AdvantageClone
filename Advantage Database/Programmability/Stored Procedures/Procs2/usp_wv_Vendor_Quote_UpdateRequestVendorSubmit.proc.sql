

CREATE PROCEDURE [dbo].[usp_wv_Vendor_Quote_UpdateRequestVendorSubmit]
(
	@ESTIMATE_NUMBER INT,
	@EST_COMPONENT_NBR SMALLINT,
	@VENDOR_QTE_NBR INT,
	@VN_CODE VARCHAR(6),
	@DISPATCH_DATE SMALLDATETIME,
	@SUBMITTED_BY VARCHAR(100)
)
AS
BEGIN
	SET NOCOUNT OFF

			UPDATE [VENDOR_QTE_VEN] WITH(ROWLOCK)
	            SET
					[DISPATCH_DATE] = @DISPATCH_DATE,
					[SUBMITTED_BY] = @SUBMITTED_BY
	           WHERE
		            [ESTIMATE_NUMBER] = @ESTIMATE_NUMBER AND 
		            [EST_COMPONENT_NBR] = @EST_COMPONENT_NBR AND 
		            [VENDOR_QTE_NBR] = @VENDOR_QTE_NBR AND
					[VN_CODE] = @VN_CODE
	    
				
END





