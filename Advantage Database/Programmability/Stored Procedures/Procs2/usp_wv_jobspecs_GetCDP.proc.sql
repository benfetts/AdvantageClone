





CREATE PROCEDURE [dbo].[usp_wv_jobspecs_GetCDP] 
@JobNumber int
AS


SELECT    JOB_LOG.JOB_NUMBER, JOB_LOG.CL_CODE, ISNULL(CLIENT.CL_NAME,'') AS CL_NAME, JOB_LOG.DIV_CODE, ISNULL(DIVISION.DIV_NAME,'') AS DIV_NAME, 
                      JOB_LOG.PRD_CODE, ISNULL(PRODUCT.PRD_DESCRIPTION,'') AS PRD_DESCRIPTION
FROM         PRODUCT INNER JOIN
                      JOB_LOG ON PRODUCT.CL_CODE = JOB_LOG.CL_CODE AND PRODUCT.DIV_CODE = JOB_LOG.DIV_CODE AND 
                      PRODUCT.PRD_CODE = JOB_LOG.PRD_CODE INNER JOIN
                      DIVISION INNER JOIN
                      CLIENT ON DIVISION.CL_CODE = CLIENT.CL_CODE ON PRODUCT.CL_CODE = DIVISION.CL_CODE AND 
                      PRODUCT.DIV_CODE = DIVISION.DIV_CODE
WHERE     (JOB_LOG.JOB_NUMBER = @JobNumber)
























