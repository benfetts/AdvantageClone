	[COOP_ID]				[int] NOT NULL,
	[BILL_COOP_CODE]		[varchar](6) NULL,
	[CL_CODE]				[varchar](6) NOT NULL,
	[DIV_CODE]				[varchar](6) NOT NULL,
	[PRD_CODE]				[varchar](6) NOT NULL,
