


/* ============================================================ */
/*   View: BILLED_ET_VIEW                                       */
/* ============================================================ */
CREATE VIEW dbo.BILLED_ET_VIEW ( AR_INV_NBR, AR_INV_SEQ, AR_TYPE, ET_ID, EXT_CITY_RESALE, EXT_COUNTY_RESALE, EXT_MARKUP_AMT,
	EXT_STATE_RESALE, FNC_CODE, JOB_COMPONENT_NBR, JOB_NUMBER, LINE_TOTAL, SEQ_NBR, TOTAL_BILL, TOTAL_COST, AR_INV_DATE,
	AR_POST_PERIOD, CMP_UPD_INV_DATE, CMP_UPD_PP, CMP_CODE, CMP_IDENTIFIER, CMP_LINE_NBR) 
AS
 SELECT ALL A.AR_INV_NBR, 
	A.AR_INV_SEQ, 
	A.AR_TYPE, 
	ET_ID, 
	EXT_CITY_RESALE, 
	EXT_COUNTY_RESALE, 
	EXT_MARKUP_AMT, 
	EXT_STATE_RESALE, 
	FNC_CODE, 
	A.JOB_COMPONENT_NBR, 
	A.JOB_NUMBER, 
	LINE_TOTAL, 
	SEQ_NBR, 
	TOTAL_BILL, 
	TOTAL_COST, 
	AR_INV_DATE, 
	AR_POST_PERIOD, 
	CMP_UPD_INV_DATE, 
	CMP_UPD_PP, 
	C.CMP_CODE, 
	CMP_IDENTIFIER, 
	CMP_LINE_NBR 
   FROM EMP_TIME_DTL A, 
	ACCT_REC B, 
	JOB_LOG C 
  WHERE A.AR_INV_NBR=B.AR_INV_NBR 
    AND A.AR_INV_SEQ=B.AR_INV_SEQ 
    AND A.AR_TYPE=B.AR_TYPE 
    AND A.JOB_NUMBER=C.JOB_NUMBER



