CREATE VIEW [dbo].[CONTRACT_REPORT_DOCUMENTS_ALL]
AS
SELECT     dbo.CONTRACT_REPORT_DOCUMENTS.CONTRACT_REPORT_ID, dbo.DOCUMENTS.DOCUMENT_ID, dbo.DOCUMENTS.FILENAME, dbo.DOCUMENTS.MIME_TYPE, 
                      dbo.DOCUMENTS.DESCRIPTION, dbo.DOCUMENTS.KEYWORDS, dbo.DOCUMENTS.UPLOADED_DATE, dbo.DOCUMENTS.USER_CODE, 
                      dbo.DOCUMENTS.FILE_SIZE,  dbo.DOCUMENTS.USER_CODE AS [USER_NAME], dbo.DOCUMENTS.REPOSITORY_FILENAME
FROM         dbo.DOCUMENTS  INNER JOIN
                      dbo.CONTRACT_REPORT_DOCUMENTS ON dbo.DOCUMENTS.DOCUMENT_ID = dbo.CONTRACT_REPORT_DOCUMENTS.DOCUMENT_ID

GO
