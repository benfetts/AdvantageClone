


CREATE VIEW dbo.CP_ALERTCMTS
AS
SELECT   TOP 100 PERCENT  dbo.ALERT_COMMENT.GENERATED_DATE, dbo.ALERT_COMMENT.ALERT_ID, dbo.ALERT_COMMENT.COMMENT_ID, 
                      dbo.ALERT_COMMENT.COMMENT, dbo.ALERT_COMMENT.EMAILSENT, dbo.ALERT_COMMENT.USER_CODE, 
					  dbo.advfn_get_user_name(dbo.ALERT_COMMENT.USER_CODE) as USER_NAME
FROM         dbo.ALERT_COMMENT 
ORDER BY
       ALERT_COMMENT.GENERATED_DATE DESC,
       ALERT_COMMENT.COMMENT_ID DESC


