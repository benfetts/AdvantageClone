


CREATE VIEW dbo.CP_ALERTLIST
AS
SELECT     dbo.ALERT.ALERT_ID, dbo.ALERT.SUBJECT, dbo.ALERT.GENERATED, dbo.advfn_get_user_name(dbo.ALERT.ALERT_USER) AS USER_NAME, dbo.ALERT.PRIORITY, 
                      dbo.ALERT_TYPE.ALERT_TYPE_DESC AS TYPE, dbo.ALERT_CATEGORY.ALERT_DESC AS CATEGORY, dbo.ALERT_TYPE.ALERT_TYPE_ID, 
                      dbo.ALERT_CATEGORY.ALERT_CAT_ID,
                          (SELECT     COUNT(*) AS EXPR1
                            FROM          dbo.ALERT_ATTACHMENT
                            WHERE      (ALERT_ID = dbo.ALERT.ALERT_ID)) AS ATTACHMENTCOUNT, dbo.ALERT.ALERT_LEVEL, ISNULL(dbo.ALERT.OFFICE_CODE, '') 
                      AS OFFICE_CODE, ISNULL(dbo.ALERT.CL_CODE, '') AS CL_CODE, ISNULL(dbo.ALERT.DIV_CODE, '') AS DIV_CODE, ISNULL(dbo.ALERT.PRD_CODE, '') 
                      AS PRD_CODE, ISNULL(dbo.ALERT.CMP_CODE, '') AS CMP_CODE, dbo.ALERT.JOB_NUMBER, dbo.ALERT.JOB_COMPONENT_NBR, 
                      dbo.ALERT.EMP_CODE AS SENDER_EMP_CODE, CONVERT(char(10), dbo.CP_ALERT_RCPT.PROCESSED, 110) AS DISMISSED_DATE, 
                      dbo.CLIENT.CL_NAME, dbo.DIVISION.DIV_NAME, dbo.PRODUCT.PRD_DESCRIPTION, ISNULL(dbo.CAMPAIGN.CMP_NAME, '') AS CMP_NAME, 
                      dbo.ALERT.BODY, LOWER(dbo.ALERT.SUBJECT) AS LOWER_SUBJECT, LOWER(CAST(dbo.ALERT.BODY AS varchar)) AS LOWER_BODY, 
                      ISNULL(dbo.OFFICE.OFFICE_NAME, '') AS OFFICE_NAME, dbo.JOB_LOG.JOB_DESC, dbo.JOB_COMPONENT.JOB_COMP_DESC, 
                      dbo.CP_ALERT_RCPT.PROCESSED, dbo.CP_ALERT_RCPT.CDP_CONTACT_ID, dbo.ALERT.ALERT_USER, dbo.ALERT.CP_ALERT, 
                      dbo.CP_ALERT_RCPT.NEW_ALERT, dbo.CP_ALERT_RCPT.READ_ALERT
FROM         dbo.JOB_COMPONENT RIGHT OUTER JOIN
                      dbo.CP_USER INNER JOIN
                      dbo.CP_ALERT_RCPT ON dbo.CP_USER.CDP_CONTACT_ID = dbo.CP_ALERT_RCPT.CDP_CONTACT_ID RIGHT OUTER JOIN
                      dbo.ALERT INNER JOIN
                      dbo.ALERT_TYPE ON dbo.ALERT.ALERT_TYPE_ID = dbo.ALERT_TYPE.ALERT_TYPE_ID INNER JOIN
                      dbo.ALERT_CATEGORY ON dbo.ALERT.ALERT_CAT_ID = dbo.ALERT_CATEGORY.ALERT_CAT_ID ON 
                      dbo.CP_ALERT_RCPT.ALERT_ID = dbo.ALERT.ALERT_ID  ON 
                      dbo.JOB_COMPONENT.JOB_NUMBER = dbo.ALERT.JOB_NUMBER AND 
                      dbo.JOB_COMPONENT.JOB_COMPONENT_NBR = dbo.ALERT.JOB_COMPONENT_NBR LEFT OUTER JOIN
                      dbo.JOB_LOG ON dbo.ALERT.JOB_NUMBER = dbo.JOB_LOG.JOB_NUMBER LEFT OUTER JOIN
                      dbo.OFFICE ON dbo.ALERT.OFFICE_CODE = dbo.OFFICE.OFFICE_CODE LEFT OUTER JOIN
                      dbo.PRODUCT ON dbo.ALERT.CL_CODE = dbo.PRODUCT.CL_CODE AND dbo.ALERT.DIV_CODE = dbo.PRODUCT.DIV_CODE AND 
                      dbo.ALERT.PRD_CODE = dbo.PRODUCT.PRD_CODE LEFT OUTER JOIN
                      dbo.CAMPAIGN ON dbo.ALERT.CMP_IDENTIFIER = dbo.CAMPAIGN.CMP_IDENTIFIER AND dbo.ALERT.CL_CODE = dbo.CAMPAIGN.CL_CODE AND 
                      dbo.ALERT.DIV_CODE = dbo.CAMPAIGN.DIV_CODE AND dbo.ALERT.PRD_CODE = dbo.CAMPAIGN.PRD_CODE AND 
                      dbo.ALERT.CMP_CODE = dbo.CAMPAIGN.CMP_CODE LEFT OUTER JOIN
                      dbo.DIVISION ON dbo.ALERT.CL_CODE = dbo.DIVISION.CL_CODE AND dbo.ALERT.DIV_CODE = dbo.DIVISION.DIV_CODE LEFT OUTER JOIN
                      dbo.CLIENT ON dbo.ALERT.CL_CODE = dbo.CLIENT.CL_CODE



