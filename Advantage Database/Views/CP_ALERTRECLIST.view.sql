


CREATE VIEW dbo.CP_ALERTRECLIST
AS
SELECT     dbo.CP_ALERT_RCPT.ALERT_ID, dbo.CP_ALERT_RCPT.ALERT_RCPT_ID, dbo.CP_ALERT_RCPT.CDP_CONTACT_ID, 
                      dbo.CP_ALERT_RCPT.EMAIL_ADDRESS, dbo.CP_ALERT_RCPT.PROCESSED, dbo.CP_ALERT_RCPT.NEW_ALERT, dbo.CP_ALERT_RCPT.READ_ALERT,
					  dbo.CP_USER.USER_NAME, dbo.CDP_CONTACT_HDR.CONT_CODE, dbo.CDP_CONTACT_HDR.CONT_FML, ISNULL(CP_ALERT_RCPT.CURRENT_RCPT, 0) AS CURRENT_RCPT
FROM         dbo.CP_ALERT_RCPT LEFT OUTER JOIN
                      dbo.CP_USER ON dbo.CP_ALERT_RCPT.CDP_CONTACT_ID = dbo.CP_USER.CDP_CONTACT_ID INNER JOIN
                      dbo.CDP_CONTACT_HDR ON dbo.CP_ALERT_RCPT.CDP_CONTACT_ID = dbo.CDP_CONTACT_HDR.CDP_CONTACT_ID



