


CREATE VIEW [dbo].[CP_MEDIA_BUYER_LIST]
AS
SELECT DISTINCT [dbo].[MAGAZINE_HEADER].[BUYER]
FROM         [dbo].[MAGAZINE_HEADER]
UNION
SELECT DISTINCT [dbo].[NEWSPAPER_HEADER].[BUYER]
FROM         [dbo].[NEWSPAPER_HEADER]
UNION
SELECT DISTINCT [dbo].[INTERNET_HEADER].[BUYER]
FROM         [dbo].[INTERNET_HEADER]
UNION
SELECT DISTINCT [dbo].[OUTDOOR_HEADER].[BUYER]
FROM         [dbo].[OUTDOOR_HEADER]
UNION
SELECT DISTINCT [dbo].[RADIO_HEADER].[BUYER]
FROM         [dbo].[RADIO_HEADER]
UNION
SELECT DISTINCT [dbo].[TV_HEADER].[BUYER]
FROM         [dbo].[TV_HEADER]




