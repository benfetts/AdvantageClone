CREATE VIEW [dbo].[CP_USERS_DESKTOP_OBJECTS]
AS
SELECT     dbo.CP_USER_TAB_DESKTOP_OBJECT.USER_CODE, dbo.CP_USER_TAB_DESKTOP_OBJECT.TAB_ID, 
                      dbo.CP_USER_TAB_DESKTOP_OBJECT.DESKTOP_OBJECT_ID, dbo.CP_DESKTOP_OBJECTS.NAME, dbo.CP_DESKTOP_OBJECTS.DESCRIPTION, 
                      dbo.CP_DESKTOP_OBJECTS.SECURITY_LEVEL, dbo.CP_DESKTOP_OBJECTS.ACTIVE, dbo.CP_DESKTOP_OBJECTS.ASCX_FILENAME, 
                      dbo.CP_USER_TAB_DESKTOP_OBJECT.PARENT_ZONE, dbo.CP_USER_TAB_DESKTOP_OBJECT.LOAD_INDEX
FROM         dbo.CP_USER_TAB_DESKTOP_OBJECT INNER JOIN
                      dbo.CP_DESKTOP_OBJECTS ON dbo.CP_USER_TAB_DESKTOP_OBJECT.DESKTOP_OBJECT_ID = dbo.CP_DESKTOP_OBJECTS.ID
