CREATE VIEW [dbo].[V_DIVISION]
WITH SCHEMABINDING
AS
SELECT dbo.DIVISION.DIV_CODE,
	   dbo.DIVISION.DIV_NAME,
	   dbo.DIVISION.CL_CODE,
	   dbo.CLIENT.CL_NAME,  
	   [CL_ACTIVE_FLAG] = dbo.CLIENT.ACTIVE_FLAG,
	   [ACTIVE_FLAG] = CASE WHEN ISNULL(CLIENT.ACTIVE_FLAG, 0) = 1 THEN DIVISION.ACTIVE_FLAG END
FROM dbo.DIVISION
	 INNER JOIN dbo.CLIENT ON dbo.DIVISION.CL_CODE = dbo.CLIENT.CL_CODE 
	 	 
GO
