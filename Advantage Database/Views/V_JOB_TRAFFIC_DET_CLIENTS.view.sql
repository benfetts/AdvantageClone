
CREATE VIEW [dbo].[V_JOB_TRAFFIC_DET_CLIENTS]
AS

 SELECT DISTINCT JOB_NUMBER, JOB_COMPONENT_NBR, SEQ_NBR, 
		dbo.advfn_task_clients( JOB_NUMBER, JOB_COMPONENT_NBR, SEQ_NBR, 1 ) AS CLIENT_CODE_LIST,
		dbo.advfn_task_clients( JOB_NUMBER, JOB_COMPONENT_NBR, SEQ_NBR, 2 ) AS CLIENT_LNAME_LIST
   FROM dbo.JOB_TRAFFIC_DET_CNTS
