
CREATE VIEW dbo.WV_AGENCY_DESKTOP_DOCUMENTS
AS
SELECT     dbo.AGENCY_DESKTOP_DOCUMENTS.OFFICE_CODE, dbo.AGENCY_DESKTOP_DOCUMENTS.DP_TM_CODE, dbo.DOCUMENTS.DOCUMENT_ID, 
                      dbo.DOCUMENTS.FILENAME, dbo.DOCUMENTS.REPOSITORY_FILENAME, dbo.DOCUMENTS.MIME_TYPE, dbo.DOCUMENTS.DESCRIPTION, 
                      dbo.DOCUMENTS.KEYWORDS, dbo.DOCUMENTS.UPLOADED_DATE, dbo.DOCUMENTS.USER_CODE, dbo.DOCUMENTS.FILE_SIZE, 
                       dbo.DOCUMENTS.USER_CODE AS [USER_NAME],
                      dbo.DOCUMENTS.PROOFHQ_URL
FROM         dbo.AGENCY_DESKTOP_DOCUMENTS INNER JOIN
                      dbo.DOCUMENTS ON dbo.AGENCY_DESKTOP_DOCUMENTS.DOCUMENT_ID = dbo.DOCUMENTS.DOCUMENT_ID

