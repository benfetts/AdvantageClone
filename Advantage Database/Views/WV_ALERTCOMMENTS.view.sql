
CREATE VIEW [dbo].[WV_ALERTCOMMENTS]
AS

	SELECT TOP 100 PERCENT ALERT_COMMENT.GENERATED_DATE AS Generated,
		   ALERT_COMMENT.ALERT_ID AS AlertID,
		   ALERT_COMMENT.COMMENT_ID AS CommentID,
		   [dbo].[advfn_get_user_name](ALERT_COMMENT.USER_CODE) AS EmployeeName,
		   ALERT_COMMENT.COMMENT,
		   ALERT_COMMENT.EMAILSENT,
		   ALERT_COMMENT.USER_CODE,
		   ALERT.SUBJECT
	FROM   ALERT_COMMENT WITH(NOLOCK)
		   INNER JOIN ALERT WITH(NOLOCK)
				ON  ALERT_COMMENT.ALERT_ID = ALERT.ALERT_ID
	ORDER BY
		ALERT_COMMENT.ALERT_ID DESC,
		ALERT_COMMENT.GENERATED_DATE DESC,
		ALERT_COMMENT.COMMENT_ID DESC	   	
