
CREATE VIEW dbo.WV_CURRENT_DIVISION_DOCUMENTS
AS
SELECT   'Division' AS LEVEL,  dbo.DIVISION_DOCUMENTS.CL_CODE, dbo.DIVISION_DOCUMENTS.DIV_CODE, dbo.DOCUMENTS.DOCUMENT_ID, dbo.DOCUMENTS.FILENAME, 
                      dbo.DOCUMENTS.REPOSITORY_FILENAME, dbo.DOCUMENTS.MIME_TYPE, dbo.DOCUMENTS.DESCRIPTION, dbo.DOCUMENTS.KEYWORDS, 
                      dbo.DOCUMENTS.UPLOADED_DATE, dbo.DOCUMENTS.USER_CODE, dbo.DOCUMENTS.FILE_SIZE,  dbo.DOCUMENTS.USER_CODE AS [USER_NAME], 
                      dbo.DOCUMENT_TYPE.DOCUMENT_TYPE_DESC, ISNULL(dbo.DOCUMENTS.PRIVATE_FLAG, 0) AS PRIVATE_FLAG, 
                      dbo.DOCUMENTS.PROOFHQ_URL
FROM         dbo.DIVISION_DOCUMENTS INNER JOIN
                      dbo.DOCUMENTS ON dbo.DIVISION_DOCUMENTS.DOCUMENT_ID = dbo.DOCUMENTS.DOCUMENT_ID  INNER JOIN
                      dbo.DOCUMENT_TYPE ON dbo.DOCUMENTS.DOCUMENT_TYPE_ID = dbo.DOCUMENT_TYPE.DOCUMENT_TYPE_ID
WHERE     (dbo.DIVISION_DOCUMENTS.DOCUMENT_ID IN
                          (SELECT     MAX(DIVISION_DOCUMENTS_1.DOCUMENT_ID) AS DOCUMENT_ID
                            FROM          dbo.DIVISION_DOCUMENTS AS DIVISION_DOCUMENTS_1 INNER JOIN
                                                   dbo.DOCUMENTS AS DOCUMENTS_1 ON DIVISION_DOCUMENTS_1.DOCUMENT_ID = DOCUMENTS_1.DOCUMENT_ID
                            GROUP BY DIVISION_DOCUMENTS_1.DIV_CODE, DOCUMENTS_1.FILENAME))
