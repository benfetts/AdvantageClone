
CREATE VIEW dbo.WV_CURRENT_OFFICE_DOCUMENTS
AS
SELECT  'Office' AS LEVEL,   dbo.OFFICE_DOCUMENTS.OFFICE_CODE, dbo.DOCUMENTS.DOCUMENT_ID, dbo.DOCUMENTS.FILENAME, dbo.DOCUMENTS.MIME_TYPE, 
                      dbo.DOCUMENTS.DESCRIPTION, dbo.DOCUMENTS.KEYWORDS, dbo.DOCUMENTS.UPLOADED_DATE, dbo.DOCUMENTS.USER_CODE, 
                      dbo.DOCUMENTS.FILE_SIZE,  dbo.DOCUMENTS.USER_CODE AS [USER_NAME], dbo.DOCUMENTS.REPOSITORY_FILENAME, 
                      dbo.DOCUMENT_TYPE.DOCUMENT_TYPE_DESC, ISNULL(dbo.DOCUMENTS.PRIVATE_FLAG, 0) AS PRIVATE_FLAG, 
                      dbo.DOCUMENTS.PROOFHQ_URL
FROM         dbo.DOCUMENTS  INNER JOIN
                      dbo.OFFICE_DOCUMENTS ON dbo.DOCUMENTS.DOCUMENT_ID = dbo.OFFICE_DOCUMENTS.DOCUMENT_ID INNER JOIN
                      dbo.DOCUMENT_TYPE ON dbo.DOCUMENTS.DOCUMENT_TYPE_ID = dbo.DOCUMENT_TYPE.DOCUMENT_TYPE_ID
WHERE     (dbo.DOCUMENTS.DOCUMENT_ID IN
                          (SELECT     MAX(OFFICE_DOCUMENTS_1.DOCUMENT_ID) AS DOCUMENT_ID
                            FROM          dbo.OFFICE_DOCUMENTS AS OFFICE_DOCUMENTS_1 INNER JOIN
                                                   dbo.DOCUMENTS AS DOCUMENTS_1 ON OFFICE_DOCUMENTS_1.DOCUMENT_ID = DOCUMENTS_1.DOCUMENT_ID
                            GROUP BY OFFICE_DOCUMENTS_1.OFFICE_CODE, DOCUMENTS_1.FILENAME))
