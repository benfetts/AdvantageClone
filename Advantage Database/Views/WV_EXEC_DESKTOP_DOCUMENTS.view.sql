
-- View
CREATE VIEW [dbo].[WV_EXEC_DESKTOP_DOCUMENTS] /*WITH ENCRYPTION*/
AS
/*=========== QUERY ===========*/
SELECT     dbo.EXEC_DESKTOP_DOCUMENTS.OFFICE_CODE, dbo.DOCUMENTS.DOCUMENT_ID, 
                      dbo.DOCUMENTS.FILENAME, dbo.DOCUMENTS.REPOSITORY_FILENAME, dbo.DOCUMENTS.MIME_TYPE, dbo.DOCUMENTS.DESCRIPTION, 
                      dbo.DOCUMENTS.KEYWORDS, dbo.DOCUMENTS.UPLOADED_DATE, dbo.DOCUMENTS.USER_CODE, dbo.DOCUMENTS.FILE_SIZE, 
                       dbo.DOCUMENTS.USER_CODE AS [USER_NAME], dbo.EXEC_DESKTOP_DOCUMENTS.EMP_CODE,
                      dbo.DOCUMENTS.PROOFHQ_URL
FROM         dbo.EXEC_DESKTOP_DOCUMENTS INNER JOIN
                      dbo.DOCUMENTS ON dbo.EXEC_DESKTOP_DOCUMENTS.DOCUMENT_ID = dbo.DOCUMENTS.DOCUMENT_ID 
/*=========== QUERY ===========*/
