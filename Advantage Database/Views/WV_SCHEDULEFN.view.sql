
CREATE VIEW [dbo].[WV_SCHEDULEFN]
AS
SELECT     dbo.JOB_TRAFFIC_DET.JOB_NUMBER, dbo.JOB_TRAFFIC_DET.JOB_COMPONENT_NBR, dbo.JOB_TRAFFIC_DET.FNC_EST, 
                      dbo.JOB_TRAFFIC_DET_EMPS.EMP_CODE, dbo.JOB_TRAFFIC_DET.FNC_CODE, dbo.JOB_TRAFFIC_DET.EMP_CODE AS GrpEmp_Code
FROM         dbo.JOB_TRAFFIC_DET INNER JOIN
                      dbo.JOB_TRAFFIC_DET_EMPS ON dbo.JOB_TRAFFIC_DET.JOB_NUMBER = dbo.JOB_TRAFFIC_DET_EMPS.JOB_NUMBER AND 
                      dbo.JOB_TRAFFIC_DET.JOB_COMPONENT_NBR = dbo.JOB_TRAFFIC_DET_EMPS.JOB_COMPONENT_NBR AND 
                      dbo.JOB_TRAFFIC_DET.SEQ_NBR = dbo.JOB_TRAFFIC_DET_EMPS.SEQ_NBR
GROUP BY dbo.JOB_TRAFFIC_DET.JOB_NUMBER, dbo.JOB_TRAFFIC_DET.JOB_COMPONENT_NBR, dbo.JOB_TRAFFIC_DET.FNC_EST, 
                      dbo.JOB_TRAFFIC_DET_EMPS.EMP_CODE, dbo.JOB_TRAFFIC_DET.FNC_CODE, dbo.JOB_TRAFFIC_DET.EMP_CODE
HAVING      (dbo.JOB_TRAFFIC_DET_EMPS.EMP_CODE IS NOT NULL)

