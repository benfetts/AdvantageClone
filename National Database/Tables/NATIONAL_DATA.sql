CREATE TABLE [dbo].[NATIONAL_DATA] (
    [NATIONAL_DATA_ID] INT           IDENTITY (1, 1) NOT NULL,
    [DATA_TYPE]        VARCHAR (4)   NOT NULL,
    [STREAM]           VARCHAR (2)   NOT NULL,
    [IS_PRELIMINARY]   BIT           NOT NULL,
    [START_DATE]       SMALLDATETIME NOT NULL,
    [END_DATE]         SMALLDATETIME NOT NULL,
    [YEAR]             INT           NOT NULL,
    [IS_HISPANIC]      BIT           NOT NULL,
    [MARKET_BREAK_ID]  INT           NOT NULL,
    [IS_CORRECTION]    BIT           NOT NULL,
    [IS_PBS_NSS]       BIT           NOT NULL,
    [FILENAME]         VARCHAR (100) NOT NULL,
    CONSTRAINT [PK_NATIONAL_DATA] PRIMARY KEY CLUSTERED ([NATIONAL_DATA_ID] ASC) WITH (FILLFACTOR = 90),
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [NATIONAL_DATA_UNIQUE]
    ON [dbo].[NATIONAL_DATA]([FILENAME] ASC);

