CREATE PROCEDURE [dbo].[advsp_hosted_radio_period_revisions_load]
AS
BEGIN

	SELECT
		[ID] = R.NIELSEN_RADIO_PERIOD_REVISION_ID,
		[OldNielsenRadioPeriodID] = R.OLD_NIELSEN_RADIO_PERIOD_ID,
		[NewNielsenRadioPeriodID] = R.NEW_NIELSEN_RADIO_PERIOD_ID
	FROM 
		NIELSENDATASTORE.dbo.NIELSEN_RADIO_PERIOD_REVISION R

END
GO
