CREATE PROC advsp_hosted_sync_NCC_TV_CABLENET
AS

BEGIN TRAN

DELETE NIELSENHOSTED.dbo.NCC_TV_CABLENET 

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NCC_TV_CABLENET ON

INSERT INTO NIELSENHOSTED.dbo.NCC_TV_CABLENET (NCC_TV_CABLENET_ID, NETWORK_CODE, NETWORK_NAME, STATION_CODE, COMSCORE_STATION_CODE)
SELECT NCC_TV_CABLENET_ID, NETWORK_CODE, NETWORK_NAME, COMSCORE_STATION_CODE
FROM NIELSENDATASTORE.dbo.NCC_TV_CABLENET

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NCC_TV_CABLENET OFF

COMMIT TRAN
GO

GRANT EXEC ON advsp_hosted_sync_NCC_TV_CABLENET TO PUBLIC
GO