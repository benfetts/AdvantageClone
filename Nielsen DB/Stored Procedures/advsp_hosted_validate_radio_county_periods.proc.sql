CREATE PROC [dbo].[advsp_hosted_validate_radio_county_periods]
AS

UPDATE h SET h.VALIDATED = 1
FROM NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_PERIOD h
	INNER JOIN NIELSENDATASTORE.dbo.NIELSEN_RADIO_COUNTY_PERIOD h2 ON h.NIELSEN_RADIO_COUNTY_PERIOD_ID = h2.NIELSEN_RADIO_COUNTY_PERIOD_ID
WHERE h.VALIDATED = 0
AND NIELSENHOSTED.[dbo].[advfn_nielsen_radio_county_rowcount_by_period](h.NIELSEN_RADIO_COUNTY_PERIOD_ID) = NIELSENDATASTORE.[dbo].[advfn_nielsen_radio_county_rowcount_by_period](h.NIELSEN_RADIO_COUNTY_PERIOD_ID)
GO

GRANT EXEC ON [advsp_hosted_validate_radio_county_periods] TO PUBLIC
GO