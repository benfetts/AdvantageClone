CREATE PROCEDURE advsp_neilsen_tv_cume_book_delete
	@NIELSEN_TV_CUME_BOOK_ID int
AS

DELETE dbo.NIELSEN_TV_CUME_DAYPART_IMPRESSION 
WHERE NIELSEN_TV_CUME_BOOK_ID = @NIELSEN_TV_CUME_BOOK_ID

DELETE dbo.NIELSEN_TV_CUME_BOOK
WHERE NIELSEN_TV_CUME_BOOK_ID = @NIELSEN_TV_CUME_BOOK_ID
GO

GRANT EXEC ON advsp_neilsen_tv_cume_book_delete TO PUBLIC
GO