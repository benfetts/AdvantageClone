CREATE PROC [dbo].[advsp_tranfer_nielsen_radio_county_intab_to_hosted]
AS

BEGIN TRAN

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_INTAB ON

INSERT INTO NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_INTAB ( NIELSEN_RADIO_COUNTY_INTAB_ID, NIELSEN_RADIO_COUNTY_PERIOD_ID, 
PERSONS_12PLUS_INTAB, PERSONS_12TO34_INTAB, PERSONS_18PLUS_INTAB, PERSONS_18TO34_INTAB, PERSONS_25TO54_INTAB, PERSONS_35PLUS_INTAB, PERSONS_35TO64_INTAB, MALES_18PLUS_INTAB, FEMALES_18PLUS_INTAB )
SELECT NIELSEN_RADIO_COUNTY_INTAB_ID, NIELSEN_RADIO_COUNTY_PERIOD_ID, 
PERSONS_12PLUS_INTAB, PERSONS_12TO34_INTAB, PERSONS_18PLUS_INTAB, PERSONS_18TO34_INTAB, PERSONS_25TO54_INTAB, PERSONS_35PLUS_INTAB, PERSONS_35TO64_INTAB, MALES_18PLUS_INTAB, FEMALES_18PLUS_INTAB
FROM NIELSENDATASTORE.dbo.NIELSEN_RADIO_COUNTY_INTAB
WHERE NIELSEN_RADIO_COUNTY_INTAB_ID NOT IN (SELECT NIELSEN_RADIO_COUNTY_INTAB_ID FROM NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_INTAB)

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_INTAB OFF

COMMIT TRAN

GO
