CREATE PROC [dbo].[advsp_tranfer_nielsen_radio_county_period_to_hosted]
AS

BEGIN TRAN

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_PERIOD ON

INSERT INTO NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_PERIOD ( NIELSEN_RADIO_COUNTY_PERIOD_ID, NIELSEN_RADIO_MARKET_NUMBER, COUNTY_CODE, [YEAR],
[START_DATE], END_DATE, IS_CLUSTER, NAME, [STATE], WEIGHTING_FLAG, MEASUREMENT_TYPE, COPYRIGHT, VALIDATED )
SELECT NIELSEN_RADIO_COUNTY_PERIOD_ID, NIELSEN_RADIO_MARKET_NUMBER, COUNTY_CODE, [YEAR],
[START_DATE], END_DATE, IS_CLUSTER, NAME, [STATE], WEIGHTING_FLAG, MEASUREMENT_TYPE, COPYRIGHT, VALIDATED
FROM NIELSENDATASTORE.dbo.NIELSEN_RADIO_COUNTY_PERIOD
WHERE NIELSEN_RADIO_COUNTY_PERIOD_ID NOT IN (SELECT NIELSEN_RADIO_COUNTY_PERIOD_ID FROM NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_PERIOD)

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_COUNTY_PERIOD OFF

COMMIT TRAN

GO
