CREATE PROC [dbo].[advsp_tranfer_nielsen_radio_market_to_hosted]
AS

BEGIN TRAN

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_MARKET ON

INSERT INTO NIELSENHOSTED.dbo.NIELSEN_RADIO_MARKET (NUMBER, [NAME], NIELSEN_RADIO_MARKET_ID, [SOURCE])
SELECT NUMBER, [NAME], NIELSEN_RADIO_MARKET_ID, [SOURCE]
FROM NIELSENDATASTORE.dbo.NIELSEN_RADIO_MARKET
WHERE NIELSEN_RADIO_MARKET_ID NOT IN (SELECT NIELSEN_RADIO_MARKET_ID FROM NIELSENHOSTED.dbo.NIELSEN_RADIO_MARKET)

SET IDENTITY_INSERT NIELSENHOSTED.dbo.NIELSEN_RADIO_MARKET OFF

COMMIT TRAN

GO


