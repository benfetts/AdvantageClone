CREATE TABLE [dbo].[CLIENT] (
    [CLIENT_ID]   INT          IDENTITY (1, 1) NOT NULL,
    [CODE]        VARCHAR (6)  NOT NULL,
    [CLIENT_NAME] VARCHAR (40) NOT NULL,
    [IS_INACTIVE] BIT          NOT NULL,
	[IS_NCC_SUBSCRIBED] BIT    NOT NULL DEFAULT(0),
    [FUSION_METERED_MARKETS] BIT NOT NULL DEFAULT (0), 
    [FUSION_DIARY_MARKETS] BIT NOT NULL DEFAULT (0), 
    CONSTRAINT [PK_CLIENT] PRIMARY KEY CLUSTERED ([CLIENT_ID] ASC)
);


GO
CREATE UNIQUE NONCLUSTERED INDEX [UIDX_CLIENT_CLIENT_NAME]
    ON [dbo].[CLIENT]([CLIENT_NAME] ASC);

