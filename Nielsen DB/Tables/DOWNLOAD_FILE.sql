CREATE TABLE [dbo].[DOWNLOAD_FILE] (
    [DOWNLOAD_FILE_ID] INT           IDENTITY (1, 1) NOT NULL,
    [FILENAME]         VARCHAR (200) NOT NULL,
    [FILETYPE]         SMALLINT      NOT NULL,
    [LAST_WRITE_TIME]  DATETIME NOT NULL,
    [PROCESSED_TIME]   SMALLDATETIME NULL,
    CONSTRAINT [PK_PROCESSED_FILE] PRIMARY KEY CLUSTERED ([DOWNLOAD_FILE_ID] ASC)
);

