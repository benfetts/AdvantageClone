CREATE PROCEDURE [dbo].[advsp_media_rfp_markets]
	@MEDIA_RFP_HEADER_ID int
AS

SELECT DISTINCT 
	[SourceMarketName] = MRD.SOURCE_MARKET_NAME,
	[MarketCode] = MRD.MARKET_CODE,
	[MediaRFPHeaderID] = MRH.MEDIA_RFP_HEADER_ID
FROM dbo.MEDIA_RFP_DEMO MRD
	INNER JOIN dbo.MEDIA_RFP_AVAILABLE_LINE MRAL ON MRD.MEDIA_RFP_AVAILABLE_LINE_ID = MRAL.MEDIA_RFP_AVAILABLE_LINE_ID 
	INNER JOIN dbo.MEDIA_RFP_HEADER MRH ON MRH.MEDIA_RFP_HEADER_ID = MRAL.MEDIA_RFP_HEADER_ID AND MRH.MEDIA_RFP_HEADER_ID = @MEDIA_RFP_HEADER_ID
WHERE
	MRD.SOURCE_MARKET_NAME IS NOT NULL
GO
