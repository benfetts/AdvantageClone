IF  EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[PROOFING_STATUS]') AND type in (N'U'))
BEGIN

	 IF NOT EXISTS (SELECT 1 FROM PROOFING_STATUS WHERE [NAME] = 'Approve')
	 BEGIN
		INSERT INTO PROOFING_STATUS WITH(ROWLOCK) ([NAME], IS_USER_DEFINED, IS_INACTIVE, SEQ_NBR)
		VALUES('Approve', 0, 0, 0);
	 END
	 IF NOT EXISTS (SELECT 1 FROM PROOFING_STATUS WHERE [NAME] = 'Defer')
	 BEGIN
		INSERT INTO PROOFING_STATUS WITH(ROWLOCK) ([NAME], IS_USER_DEFINED, IS_INACTIVE, SEQ_NBR)
		VALUES('Defer', 0, 1, 1);
	 END
	 IF NOT EXISTS (SELECT 1 FROM PROOFING_STATUS WHERE [NAME] = 'Reject')
	 BEGIN
		INSERT INTO PROOFING_STATUS WITH(ROWLOCK) ([NAME], IS_USER_DEFINED, IS_INACTIVE, SEQ_NBR)
		VALUES('Reject', 0, 0, 2);
	 END

END
