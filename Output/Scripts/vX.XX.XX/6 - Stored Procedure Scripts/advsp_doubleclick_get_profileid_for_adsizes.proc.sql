CREATE PROC [dbo].[advsp_doubleclick_get_profileid_for_adsizes]
AS

DECLARE @PROFILE_ID bigint

IF EXISTS(SELECT 1 FROM dbo.AGY_SETTINGS WHERE AGY_SETTINGS_CODE = 'DC_ENABLED' AND AGY_SETTINGS_VALUE = 1)
BEGIN
	SELECT @PROFILE_ID = CAST(AGY_SETTINGS_VALUE as bigint) FROM dbo.AGY_SETTINGS WHERE AGY_SETTINGS_CODE = 'DC_PROFILE_ID'
END
ELSE
BEGIN
	SELECT TOP 1 @PROFILE_ID = DC_PROFILE_ID FROM dbo.CLIENT
END

SELECT ISNULL(@PROFILE_ID, 0)

GO
