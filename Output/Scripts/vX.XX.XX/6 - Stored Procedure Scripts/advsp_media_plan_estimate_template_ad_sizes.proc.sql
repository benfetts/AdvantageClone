CREATE PROCEDURE [dbo].[advsp_media_plan_estimate_template_ad_sizes]
	@MEDIA_PLAN_ESTIMATE_TEMPLATE_ID int,
	@MEDIA_TYPE varchar(1)
AS

SELECT
	Selected = CAST(CASE WHEN mpetas.SIZE_CODE IS NOT NULL THEN 1 ELSE 0 END as bit),
	AdSizeCode = a.SIZE_CODE,
	AdSizeDescription = a.SIZE_DESC 
FROM dbo.AD_SIZE a
	LEFT OUTER JOIN dbo.MEDIA_PLAN_ESTIMATE_TEMPLATE_AD_SIZE mpetas ON a.SIZE_CODE = mpetas.SIZE_CODE AND mpetas.MEDIA_PLAN_ESTIMATE_TEMPLATE_ID = @MEDIA_PLAN_ESTIMATE_TEMPLATE_ID
WHERE COALESCE(a.INACTIVE_FLAG,0) = 0
AND a.MEDIA_TYPE = @MEDIA_TYPE
ORDER BY a.SIZE_DESC
GO
