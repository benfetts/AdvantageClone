IF (SELECT COUNT(1) FROM dbo.VENDOR_DCM_MAPPING) = 0 BEGIN

	DECLARE @DC_PROFILE_ID bigint

	SELECT @DC_PROFILE_ID = CAST(AGY_SETTINGS_VALUE as bigint) FROM dbo.AGY_SETTINGS 
	WHERE AGY_SETTINGS_CODE = 'DC_PROFILE_ID'

	IF @DC_PROFILE_ID IS NOT NULL
		INSERT INTO dbo.VENDOR_DCM_MAPPING (VN_CODE, DC_PROFILE_ID, DC_SITE_ID)
		SELECT VN_CODE, @DC_PROFILE_ID, SOURCE_VN_CODE 
		FROM dbo.VENDOR_XREF
		WHERE RECORD_SOURCE_ID = (SELECT RECORD_SOURCE_ID FROM dbo.RECORD_SOURCE WHERE [NAME] IN ( 'DoubleClick Advertiser ID', 'DoubleClick' ) AND SYSTEM_SOURCE = 1)

END

UPDATE dbo.RECORD_SOURCE SET [NAME] = 'DoubleClick', [DESCRIPTION] = 'DoubleClick Mappings'
WHERE [NAME] = 'DoubleClick Advertiser ID'
AND SYSTEM_SOURCE = 1

GO
