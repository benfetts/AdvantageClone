DECLARE @TEMPLATE_ID int

IF EXISTS(SELECT 1 FROM dbo.MEDIA_PLAN_ESTIMATE_TEMPLATE WHERE [TYPE] = 'I' AND IS_SYSTEM = 1 AND [DESCRIPTION] = 'Video Focused Branding') BEGIN

	SELECT @TEMPLATE_ID = MEDIA_PLAN_ESTIMATE_TEMPLATE_ID
    FROM dbo.MEDIA_PLAN_ESTIMATE_TEMPLATE WHERE [TYPE] = 'I' AND IS_SYSTEM = 1 AND [DESCRIPTION] = 'Video Focused Branding'

	--tactics
	UPDATE dbo.MEDIA_PLAN_ESTIMATE_TEMPLATE_TACTIC SET [DESCRIPTION] = 'Geo-Optimized'
	WHERE MEDIA_PLAN_ESTIMATE_TEMPLATE_ID = @TEMPLATE_ID
    AND [DESCRIPTION] = 'Pre-roll'

END
